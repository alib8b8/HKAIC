"""
AI守护者模块 (ai_guardian.py)
==============================
交易安全检测引擎：合约风险、地址风险、金额异常、频率异常、授权风险。
风险等级：安全/注意/警告/危险，高风险需二次确认。
钱包锁：自动锁定+异常行为主动锁定。
纯Python标准库，零外部依赖。
"""

import hashlib
import math
import time
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple
from enum import Enum


class 风险等级(Enum):
    """交易风险等级"""
    安全 = "safe"         # 绿色
    注意 = "caution"      # 黄色
    警告 = "warning"      # 橙色
    危险 = "danger"       # 红色


@dataclass
class 风险项:
    """单个风险检测项"""
    类别: str        # 合约/地址/金额/频率/授权
    描述: str        # 风险描述
    等级: 风险等级   # 该项风险等级
    建议: str = ""   # AI建议


@dataclass
class 风险报告:
    """完整风险检测报告"""
    交易哈希: str = ""
    综合等级: 风险等级 = 风险等级.安全
    风险项列表: List[风险项] = field(default_factory=list)
    AI总结: str = ""
    需要二次确认: bool = False
    检测时间: float = 0.0

    def __post_init__(self):
        if self.检测时间 == 0:
            self.检测时间 = time.time()


class AI守护者:
    """
    AI守护者 - 交易安全实时检测引擎

    五维检测：
      1. 合约交互风险：未验证合约、钓鱼特征
      2. 地址风险：黑名单、新地址、攻击者关联
      3. 金额异常：偏离历史均值N个标准差
      4. 频率异常：短时间内频繁转账
      5. 授权风险：approve无限额度

    钱包锁：
      - 离开N分钟自动锁定
      - AI检测到异常行为主动锁定
    """

    def __init__(self, 灵敏度: float = 0.8, 自动锁定分钟: int = 5):
        self.灵敏度 = 灵敏度  # 0-1，越高越严格
        self.自动锁定分钟 = 自动锁定分钟
        # 历史交易统计
        self._交易历史: List[Dict] = []  # 简化交易记录
        self._交易金额列表: List[float] = []
        self._交易时间列表: List[float] = []
        # 风险数据库
        self._黑名单: set = set()
        self._已验证合约: set = set()
        self._已知攻击者: set = set()
        self._新地址集合: set = set()     # 最近7天内首次出现
        # 钱包锁状态
        self._已锁定: bool = False
        self._上次活跃时间: float = time.time()
        self._锁定原因: str = ""
        # 金额统计
        self._历史均值: float = 0.0
        self._历史标准差: float = 0.0

    # ========== 合约交互风险检测 ==========

    def 注册已验证合约(self, 合约地址: str):
        """注册已验证的安全合约"""
        self._已验证合约.add(合约地址.lower())

    def 检测合约风险(self, 合约地址: str) -> 风险项:
        """
        检测合约交互风险
        - 是否调用未验证合约
        - 是否有钓鱼特征（地址相似度检测）
        """
        合约地址 = 合约地址.lower()
        if 合约地址 in self._已验证合约:
            return 风险项(
                类别="合约",
                描述="已验证合约",
                等级=风险等级.安全,
                建议="合约代码已验证，安全可信",
            )
        # 未验证合约 - 注意级别
        if self.灵敏度 >= 0.7:
            return 风险项(
                类别="合约",
                描述="未验证合约",
                等级=风险等级.注意,
                建议="该合约代码未经验证，交互前请确认合约来源。建议在浏览器中查看合约详情。",
            )
        return 风险项(
            类别="合约",
            描述="未验证合约（灵敏度低）",
            等级=风险等级.安全,
            建议="灵敏度较低，仅做提醒。",
        )

    # ========== 地址风险检测 ==========

    def 加入黑名单(self, 地址: str):
        """将地址加入黑名单"""
        self._黑名单.add(地址.lower())

    def 加入已知攻击者(self, 地址: str):
        """将地址标记为已知攻击者"""
        self._已知攻击者.add(地址.lower())

    def 检测地址风险(self, 目标地址: str) -> 风险项:
        """
        检测目标地址风险
        - 是否在黑名单
        - 是否是已知攻击者
        - 是否是新地址
        """
        地址 = 目标地址.lower()
        if 地址 in self._黑名单:
            return 风险项(
                类别="地址",
                描述=f"黑名单地址 {目标地址[:10]}...",
                等级=风险等级.危险,
                建议="该地址已被标记为黑名单地址，强烈建议取消交易！",
            )
        if 地址 in self._已知攻击者:
            return 风险项(
                类别="地址",
                描述=f"已知攻击者 {目标地址[:10]}...",
                等级=风险等级.危险,
                建议="该地址与已知攻击者关联，请立即取消交易！",
            )
        if 地址 in self._新地址集合:
            return 风险项(
                类别="地址",
                描述=f"新地址（7天内首次出现）{目标地址[:10]}...",
                等级=风险等级.注意,
                建议="该地址近期才首次出现，请确认收款人身份。",
            )
        return 风险项(
            类别="地址",
            描述="地址安全",
            等级=风险等级.安全,
            建议="地址未发现风险标记。",
        )

    # ========== 金额异常检测 ==========

    def 更新金额统计(self, 金额: float):
        """更新历史交易金额统计"""
        self._交易金额列表.append(金额)
        # 只保留最近100笔
        if len(self._交易金额列表) > 100:
            self._交易金额列表 = self._交易金额列表[-100:]
        self._更新均值标准差()

    def _更新均值标准差(self):
        """重新计算历史均值和标准差"""
        if not self._交易金额列表:
            return
        n = len(self._交易金额列表)
        self._历史均值 = sum(self._交易金额列表) / n
        if n > 1:
            方差 = sum((x - self._历史均值) ** 2 for x in self._交易金额列表) / (n - 1)
            self._历史标准差 = math.sqrt(方差)
        else:
            self._历史标准差 = 0

    def 检测金额异常(self, 金额: float, 标准差阈值: float = 3.0) -> 风险项:
        """
        检测金额异常
        偏离历史均值N个标准差以上则报警
        """
        if not self._交易金额列表 or len(self._交易金额列表) < 3:
            return 风险项(
                类别="金额",
                描述="历史数据不足，无法评估",
                等级=风险等级.安全,
                建议="交易记录较少，AI守护者将在积累更多数据后提供更准确的风险评估。",
            )
        if self._历史标准差 == 0:
            return 风险项(
                类别="金额",
                描述="历史交易金额一致",
                等级=风险等级.安全,
            )
        偏离 = abs(金额 - self._历史均值) / self._历史标准差
        if 偏离 > 标准差阈值 * 1.5:
            return 风险项(
                类别="金额",
                描述=f"金额严重异常：{金额:.2f} HKAIC，偏离历史均值 {偏离:.1f}σ",
                等级=风险等级.危险,
                建议=f"此金额远超历史均值({self._历史均值:.2f})，请仔细确认！",
            )
        elif 偏离 > 标准差阈值:
            return 风险项(
                类别="金额",
                描述=f"金额偏高：{金额:.2f} HKAIC，偏离历史均值 {偏离:.1f}σ",
                等级=风险等级.警告,
                建议=f"此金额超出历史均值({self._历史均值:.2f})较多，请确认是否正确。",
            )
        return 风险项(
            类别="金额",
            描述="金额正常",
            等级=风险等级.安全,
        )

    # ========== 频率异常检测 ==========

    def 检测频率异常(self, 窗口秒: float = 60.0, 频率阈值: int = 5) -> 风险项:
        """
        检测短时间内频繁转账
        """
        now = time.time()
        cutoff = now - 窗口秒
        最近交易 = [t for t in self._交易时间列表 if t > cutoff]
        频率 = len(最近交易)
        if 频率 >= 频率阈值 * 2:
            return 风险项(
                类别="频率",
                描述=f"交易频率极高：{窗口秒:.0f}秒内{频率}笔交易",
                等级=风险等级.危险,
                建议="检测到异常高频交易，可能是被盗！建议立即锁定钱包！",
            )
        elif 频率 >= 频率阈值:
            return 风险项(
                类别="频率",
                描述=f"交易频率偏高：{窗口秒:.0f}秒内{频率}笔交易",
                等级=风险等级.警告,
                建议="短时间内多笔交易，请确认是否本人操作。",
            )
        return 风险项(
            类别="频率",
            描述="交易频率正常",
            等级=风险等级.安全,
        )

    # ========== 授权风险检测 ==========

    def 检测授权风险(self, approve额度: float, 代币总量: float = 0.0) -> 风险项:
        """
        检测approve是否设置无限额度
        """
        if 代币总量 > 0 and approve额度 > 代币总量:
            return 风险项(
                类别="授权",
                描述="无限授权：approve额度超过代币总量",
                等级=风险等级.危险,
                建议="切勿设置无限approve！建议仅授权所需金额。",
            )
        # 检查超大额度 (超过10^18 视为无限授权模式)
        if approve额度 > 1e18:
            return 风险项(
                类别="授权",
                描述="疑似无限授权：approve额度极大",
                等级=风险等级.警告,
                建议="检测到超大授权额度，建议仅授权实际需要的金额。",
            )
        return 风险项(
            类别="授权",
            描述="授权额度合理",
            等级=风险等级.安全,
        )

    # ========== 综合检测 ==========

    def 检测交易(self, 目标地址: str, 金额: float,
                  合约地址: str = "", approve额度: float = 0.0,
                  标准差阈值: float = 3.0, 频率窗口秒: float = 60.0,
                  频率阈值: int = 5) -> 风险报告:
        """
        对一笔交易进行全面风险检测
        返回综合风险报告
        """
        风险项列表 = []
        # 1. 合约风险
        if 合约地址:
            风险项列表.append(self.检测合约风险(合约地址))
        # 2. 地址风险
        风险项列表.append(self.检测地址风险(目标地址))
        # 3. 金额异常
        风险项列表.append(self.检测金额异常(金额, 标准差阈值))
        # 4. 频率异常
        风险项列表.append(self.检测频率异常(频率窗口秒, 频率阈值))
        # 5. 授权风险
        if approve额度 > 0:
            风险项列表.append(self.检测授权风险(approve额度))

        # 综合评级：取最高风险等级
        等级优先级 = {风险等级.安全: 0, 风险等级.注意: 1, 风险等级.警告: 2, 风险等级.危险: 3}
        最高等级 = 风险等级.安全
        for 项 in 风险项列表:
            if 等级优先级[项.等级] > 等级优先级[最高等级]:
                最高等级 = 项.等级

        # AI总结
        AI总结 = self._生成AI总结(风险项列表, 最高等级)

        # 是否需要二次确认
        需要确认 = 最高等级 in (风险等级.警告, 风险等级.危险)

        # 如果危险，主动锁定
        if 最高等级 == 风险等级.危险 and self.灵敏度 >= 0.8:
            self._锁定("AI守护者检测到高危交易")

        return 风险报告(
            综合等级=最高等级,
            风险项列表=风险项列表,
            AI总结=AI总结,
            需要二次确认=需要确认,
        )

    def _生成AI总结(self, 风险项列表: List[风险项], 综合等级: 风险等级) -> str:
        """AI生成风险总结"""
        等级文案 = {
            风险等级.安全: "✅ 交易安全，未检测到风险。",
            风险等级.注意: "⚠️ 存在需要注意的细节，建议确认后继续。",
            风险等级.警告: "⚠️⚠️ 检测到显著风险，强烈建议仔细确认！",
            风险等级.危险: "🚨 检测到高危风险！建议立即取消交易！",
        }
        总结 = 等级文案.get(综合等级, "")
        风险项描述 = [f"[{项.类别}]{项.描述}" for 项 in 风险项列表 if 项.等级 != 风险等级.安全]
        if 风险项描述:
            总结 += " 风险点：" + "；".join(风险项描述)
        return 总结

    # ========== 钱包锁 ==========

    def 记录活跃(self):
        """记录用户活跃时间"""
        self._上次活跃时间 = time.time()
        if self._已锁定 and self._锁定原因 == "自动锁定":
            self._已锁定 = False
            self._锁定原因 = ""

    def _锁定(self, 原因: str):
        """锁定钱包"""
        self._已锁定 = True
        self._锁定原因 = 原因

    def 解锁(self):
        """手动解锁"""
        self._已锁定 = False
        self._锁定原因 = ""
        self._上次活跃时间 = time.time()

    def 检查自动锁定(self) -> bool:
        """检查是否需要自动锁定"""
        if self._已锁定:
            return True
        空闲时间 = time.time() - self._上次活跃时间
        if 空闲时间 > self.自动锁定分钟 * 60:
            self._锁定("自动锁定")
            return True
        return False

    @property
    def 已锁定(self) -> bool:
        return self._已锁定

    @property
    def 锁定原因(self) -> str:
        return self._锁定原因

    # ========== 记录交易 ==========

    def 记录交易(self, 目标地址: str, 金额: float):
        """记录一笔交易到历史"""
        self._交易历史.append({
            "地址": 目标地址,
            "金额": 金额,
            "时间": time.time(),
        })
        self._交易时间列表.append(time.time())
        self.更新金额统计(金额)
        self.记录活跃()

    # ========== 风险等级颜色 ==========

    def 等级颜色(self, 等级: 风险等级) -> str:
        """获取风险等级终端颜色代码"""
        颜色 = {
            风险等级.安全: "\033[92m",    # 绿色
            风险等级.注意: "\033[93m",    # 黄色
            风险等级.警告: "\033[33m",    # 橙色
            风险等级.危险: "\033[91m",    # 红色
        }
        return 颜色.get(等级, "\033[0m")
