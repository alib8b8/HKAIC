        总览 = self._钱包.资产总览()
        信用 = self._钱包.信用分引擎.获取信用分(账户.EVM地址)

        # 资产面板
        资产行 = [
            f"  EVM地址:  {账户.EVM地址}",
            f"  鸿坤地址: {账户.鸿坤地址}",
            "",
            f"  💰 HKAIC余额:   {色.亮绿}{总览.get('HKAIC余额', 0):,.4f}{色.重置}",
            f"  🔒 质押中:      {总览.get('质押中HKAIC', 0):,.4f}",
            f"  ✅ 可用:        {总览.get('可用HKAIC', 0):,.4f}",
        ]
        print(画面板("资产总览", 资产行))

        # 信用分面板
        等级颜色 = self._钱包.信用分引擎.等级颜色代码(信用.等级)
        信用行 = [
            f"  信用分: {等级颜色}{信用.信用分:.0f}{色.重置} "
            f"{等级颜色}{self._钱包.信用分引擎.等级标识(信用.等级)}{色.重置}",
            f"  {画进度条(信用.信用分, 1000)}",
            f"  链上行为: {信用.链上行为分:.0f}/250  PoEI: {信用.PoEI涌现分:.0f}/250",
            f"  网络中心度: {信用.网络中心度:.0f}/200  协作: {信用.协作指数:.0f}/200",
            f"  风险扣分: -{信用.风险扣分:.0f}/100",
        ]
        print(画面板("涌智信用分", 信用行))

        # AI守护者状态
        锁定状态 = f"{色.亮红}🔒 已锁定{色.重置}" if self._钱包.AI守护.已锁定 else f"{色.亮绿}🔓 未锁定{色.重置}"
        守护行 = [
            f"  状态: {锁定状态}",
            f"  灵敏度: {self._钱包.AI守护.灵敏度:.0%}",
            f"  自动锁定: {self._钱包.AI守护.自动锁定分钟}分钟",
        ]
        print(画面板("AI守护者", 守护行))

