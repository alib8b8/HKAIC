# ========== UI组件 ==========

def 画分隔线(宽: int = 55, 字符: str = "─"):
    """画分隔线"""
    return 字符 * 宽


def 画标题(文本: str, 宽: int = 55):
    """画标题行"""
    内边距 = 宽 - len(文本) - 2
    左 = 内边距 // 2
    右 = 内边距 - 左
    return "│" + " " * 左 + 文本 + " " * 右 + "│"


def 画面板(标题: str, 内容行: List[str], 宽: int = 55) -> str:
    """画一个面板"""
    线 = []
    线.append("╭" + "─" * (宽 - 2) + "╮")
    线.append(画标题(标题, 宽))
    线.append("├" + "─" * (宽 - 2) + "┤")
    for 行 in 内容行:
        if len(行) > 宽 - 4:
            行 = 行[:宽 - 7] + "..."
        线.append("│ " + 行 + " " * max(0, 宽 - 4 - len(行)) + " │")
    线.append("╰" + "─" * (宽 - 2) + "╯")
    return "\n".join(线)


def 画进度条(当前: float, 总计: float, 宽: int = 30) -> str:
    """画进度条"""
    if 总计 <= 0:
        return "[" + "░" * 宽 + "]"
    比例 = min(1.0, 当前 / 总计)
    填充 = int(比例 * 宽)
    return "[" + "█" * 填充 + "░" * (宽 - 填充) + f"] {比例:.0%}"


