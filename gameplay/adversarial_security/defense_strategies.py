@dataclass
class 防御参数:
    """防御策略参数"""
    灵敏度: float = 0.7            # 0-1，检测灵敏度（高=更多拦截但可能误报）
    响应时间秒: float = 5.0         # 响应延迟
    资源消耗: float = 100.0         # 维持该策略的资源消耗
    最大并发: int = 1000            # 最大并发处理量
    自适应: bool = True             # 是否根据攻击自适应调整


@dataclass
class 防御策略:
    """单个防御策略定义"""
    策略ID: str
    类型: 防御类型
    名称: str
    描述: str
    参数: 防御参数 = field(default_factory=防御参数)
    防御层级: 攻击层级 = 攻击层级.交易层  # 防御针对的攻击层级
    有效攻击类型: List[攻击类型] = field(default_factory=list)  # 有效的攻击类型
    拦截率: float = 0.7            # 0-1，对该类攻击的拦截概率
    误报率: float = 0.05           # 0-1，误报概率
    响应步骤: List[str] = field(default_factory=list)  # 防御响应步骤
    资源开销: float = 0.3          # 0-1，资源开销比例

    def 防御评分(self) -> float:
        """计算防御评分——基于拦截率和误报率

        评分 = 拦截率 × 100 × (1 - 误报率 × 2)
        高拦截率+低误报率=高评分
        """
        基础分 = self.拦截率 * 100
        误报惩罚 = self.误报率 * 2  # 误报率权重加倍
        灵敏度调整 = 0.8 + self.参数.灵敏度 * 0.2
        return max(0, 基础分 * (1 - 误报惩罚) * 灵敏度调整)

    def 对攻击拦截概率(self, 攻击: 攻击场景) -> float:
        """计算对特定攻击的拦截概率

        考虑防御有效攻击类型匹配和攻击检测难度
        """
        # 类型匹配
        if 攻击.类型 in self.有效攻击类型:
            类型匹配 = 1.0
        elif 攻击.层级 == self.防御层级:
            类型匹配 = 0.5  # 同层级但非直接匹配
        else:
            类型匹配 = 0.1  # 不同层级，弱防御

        # 检测难度影响
        检测因子 = 1.0 - 攻击.检测难度 * 0.5

        # 强度影响
        强度因子 = 1.0 - (攻击.参数.强度 / 10.0) * 0.3

        return self.拦截率 * 类型匹配 * 检测因子 * 强度因子


