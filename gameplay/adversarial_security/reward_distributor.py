"""
HKC 奖励分配器 (reward_distributor.py)
=======================================
对抗即安全的经济激励层。
攻击成功赚HKAIC、防御成功也赚HKAIC、发现新型攻击/防御模式额外奖励。

奖励设计：
  - 攻击奖励：发现新漏洞大额(100-1000 HKAIC)，成功攻击中等(10-100)，有价值信息小额(1-10)
  - 防御奖励：成功防御按威胁程度奖励，零漏洞连续运行奖励，防御创新奖励
  - 涌现奖励：发现新型攻击/防御模式额外奖励

奖励来源：保险池 + 铸币

纯Python标准库，零外部依赖。
"""

import hashlib
import math
import os
import time
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple
from enum import Enum

from .attack_scenarios import 攻击场景, 攻击类型, 攻击场景库
from .defense_strategies import 防御策略, 防御策略库
from .game_arena import 对战结果, 回合结果


class 奖励类型(Enum):
    """奖励类型"""
    攻击成功 = "attack_success"
    漏洞发现 = "vulnerability_discovery"
    攻击有价值 = "attack_valuable"
    防御成功 = "defense_success"
    零漏洞运行 = "zero_vuln_run"
    防御创新 = "defense_innovation"
    涌现发现 = "emergence_discovery"


@dataclass
class 奖励记录:
    """奖励记录"""
    奖励ID: str
    接收者: str
    类型: 奖励类型
    金额HKAIC: float
    关联对战: str = ""
    关联场景: str = ""
    关联策略: str = ""
    时间戳: float = 0.0
    备注: str = ""

    def __post_init__(self):
        if self.时间戳 == 0:
            self.时间戳 = time.time()


class 奖励分配器:
    """
    对抗安全游戏的奖励分配器

    核心原则：
      - 双向激励：攻击和防御都能赚钱，避免只有攻击有动力
      - 分级奖励：漏洞发现 > 成功攻击/防御 > 有价值尝试
      - 涌现激励：发现新攻击/防御模式额外奖励
      - 持续激励：零漏洞连续运行奖励鼓励长期防御

    奖励来源：
      - 保险池：跨链保险池的一部分
      - 铸币：新增HKAIC（有通胀上限）

    与PoEI联动：
      - 涌现分数高的参与者获得奖励加成
      - 奖励金额影响信用分
    """

    # 奖励金额范围（HKAIC）
    漏洞发现奖励范围 = (100.0, 1000.0)
    攻击成功奖励范围 = (10.0, 100.0)
    有价值信息奖励范围 = (1.0, 10.0)
    防御成功基础奖励 = 15.0
    零漏洞每日奖励 = 5.0
    防御创新奖励 = 50.0
    涌现发现奖励 = 200.0

    # 铸币上限
    年铸币上限 = 1000000.0  # 每年最大铸币量

    def __init__(self, 保险池余额: float = 0.0):
        """初始化奖励分配器

        参数:
            保险池余额: 初始保险池余额
        """
        self._保险池 = 保险池余额
        self._已铸币 = 0.0
        self._奖励历史: List[奖励记录] = []
        self._参与者累计: Dict[str, float] = {}  # 地址 -> 累计奖励
        self._涌现分数: Dict[str, float] = {}    # 地址 -> 涌现分数

    @property
    def 保险池余额(self) -> float:
        return self._保险池

    @property
    def 已铸币总额(self) -> float:
        return self._已铸币

    def 更新涌现分数(self, 地址: str, 分数: float):
        """更新参与者涌现分数（奖励加成用）"""
        self._涌现分数[地址] = 分数

    def 计算攻击奖励(self, 场景: 攻击场景, 成功: bool,
                      是新漏洞: bool = False, 有价值信息: bool = False) -> float:
        """计算攻击者奖励

        参数:
            场景: 攻击场景
            成功: 是否攻击成功
            是新漏洞: 是否发现新漏洞
            有价值信息: 攻击失败但有价值信息
        返回:
            奖励金额（HKAIC）
        """
        if 是新漏洞:
            # 发现新漏洞：大额奖励，基于威胁等级
            基础 = self.漏洞发现奖励范围[0]
            上限 = self.漏洞发现奖励范围[1]
            按威胁 = 基础 + (上限 - 基础) * (场景.威胁等级 / 10.0)
            创新加成 = 1.0 + 场景.创新性 * 0.5
            return 按威胁 * 创新加成

        if 成功:
            # 攻击成功：中等奖励
            基础 = self.攻击成功奖励范围[0]
            上限 = self.攻击成功奖励范围[1]
            按威胁 = 基础 + (上限 - 基础) * (场景.威胁等级 / 10.0)
            强度加成 = 0.5 + 场景.参数.强度 / 20.0
            return 按威胁 * 强度加成

        if 有价值信息:
            # 有价值信息：小额奖励
            基础 = self.有价值信息奖励范围[0]
            上限 = self.有价值信息奖励范围[1]
            创新加成 = 1.0 + 场景.创新性 * 0.3
            return 基础 + (上限 - 基础) * 场景.创新性 * 创新加成

        return 0.0

    def 计算防御奖励(self, 策略列表: List[防御策略], 攻击: 攻击场景,
                      成功: bool) -> float:
        """计算防御者奖励

        参数:
            策略列表: 使用的防御策略列表
            攻击: 被防御的攻击场景
            成功: 是否防御成功
        返回:
            奖励金额（HKAIC）
        """
        if not 成功:
            return 0.0

        # 基础奖励 + 威胁程度加成
        威胁加成 = 1.0 + (攻击.威胁等级 / 10.0) * 2.0
        奖励 = self.防御成功基础奖励 * 威胁加成

        # 策略评分加成
        if 策略列表:
            平均评分 = sum(s.防御评分() for s in 策略列表) / len(策略列表)
            奖励 *= (0.5 + 平均评分 / 100.0 * 0.5)

        return 奖励

    def 分配对战奖励(self, 对战: 对战结果,
                       攻击场景映射: Optional[Dict[str, 攻击场景]] = None,
                       防御策略映射: Optional[Dict[str, 防御策略]] = None) -> List[奖励记录]:
        """根据对战结果分配奖励

        参数:
            对战: 对战结果
            攻击场景映射: 场景ID->场景对象的映射
            防御策略映射: 策略ID->策略对象的映射
        返回:
            奖励记录列表
        """
        奖励列表 = []
        场景映射 = 攻击场景映射 or {}
        策略映射 = 防御策略映射 or {}

        for 回合 in 对战.回合记录:
            # 攻击者奖励
            if 回合.结果 == 回合结果.攻击成功:
                场景 = 场景映射.get(回合.攻击场景)
                if 场景:
                    金额 = self.计算攻击奖励(场景, 成功=True)
                else:
                    金额 = self.攻击成功奖励范围[0]

                # 涌现分数加成
                E_i = self._涌现分数.get(回合.攻击者, 1.0)
                金额 *= (1 + math.log(max(E_i, 1.0)) * 0.1)

                奖励 = self._创建奖励(
                    接收者=回合.攻击者,
                    类型=奖励类型.攻击成功,
                    金额=金额,
                    关联对战=对战.对战ID,
                    关联场景=回合.攻击场景,
                )
                奖励列表.append(奖励)

            # 防御者奖励
            elif 回合.结果 == 回合结果.防御成功:
                策略列表 = [策略映射[sid] for sid in 回合.防御策略 if sid in 策略映射]
                场景 = 场景映射.get(回合.攻击场景)
                if 场景 and 策略列表:
                    金额 = self.计算防御奖励(策略列表, 场景, 成功=True)
                else:
                    金额 = self.防御成功基础奖励

                # 涌现分数加成
                E_i = self._涌现分数.get(回合.防御者, 1.0)
                金额 *= (1 + math.log(max(E_i, 1.0)) * 0.1)

                奖励 = self._创建奖励(
                    接收者=回合.防御者,
                    类型=奖励类型.防御成功,
                    金额=金额,
                    关联对战=对战.对战ID,
                    关联场景=回合.攻击场景,
                )
                奖励列表.append(奖励)

        return 奖励列表

    def 分配涌现奖励(self, 发现者: str, 发现类型: str,
                       创新性: float = 0.8) -> 奖励记录:
        """分配涌现奖励——发现新型攻击/防御模式

        参数:
            发现者: 发现者地址
            发现类型: "attack" / "defense"
            创新性: 创新性评分
        返回:
            奖励记录
        """
        金额 = self.涌现发现奖励 * (0.5 + 创新性 * 0.5)
        类型 = 奖励类型.涌现发现

        return self._创建奖励(
            接收者=发现者,
            类型=类型,
            金额=金额,
            备注=f"发现新型{发现类型}模式，创新性={创新性:.2f}",
        )

    def 分配零漏洞奖励(self, 防御者: str, 连续天数: int) -> 奖励记录:
        """分配零漏洞连续运行奖励

        参数:
            防御者: 防御者地址
            连续天数: 连续零漏洞天数
        返回:
            奖励记录
        """
        金额 = self.零漏洞每日奖励 * 连续天数
        # 连续天数加成
        金额 *= (1 + math.log(max(连续天数, 1)) * 0.2)

        return self._创建奖励(
            接收者=防御者,
            类型=奖励类型.零漏洞运行,
            金额=金额,
            备注=f"零漏洞连续运行{连续天数}天",
        )

    def _创建奖励(self, 接收者: str, 类型: 奖励类型, 金额: float,
                   关联对战: str = "", 关联场景: str = "",
                   关联策略: str = "", 备注: str = "") -> 奖励记录:
        """创建奖励记录并扣款/铸币"""
        # 来源优先级：保险池 -> 铸币
        if self._保险池 >= 金额:
            self._保险池 -= 金额
            来源 = "保险池"
        else:
            # 铸币
            可铸币 = self.年铸币上限 - self._已铸币
            if 可铸币 >= 金额:
                self._已铸币 += 金额
                来源 = "铸币"
            else:
                金额 = 可铸币
                self._已铸币 += 金额
                来源 = "铸币（已达上限）"

        奖励ID = hashlib.sha256(
            f"reward:{接收者}:{类型.value}:{os.urandom(16).hex()}".encode()
        ).hexdigest()[:24]

        记录 = 奖励记录(
            奖励ID=奖励ID,
            接收者=接收者,
            类型=类型,
            金额HKAIC=金额,
            关联对战=关联对战,
            关联场景=关联场景,
            关联策略=关联策略,
            备注=备注 or f"来源: {来源}",
        )

        self._奖励历史.append(记录)
        self._参与者累计[接收者] = self._参与者累计.get(接收者, 0) + 金额

        return 记录

    def 充值保险池(self, 金额: float):
        """向保险池充值"""
        self._保险池 += 金额

    def 获取参与者累计(self, 地址: str) -> float:
        """获取参与者累计奖励"""
        return self._参与者累计.get(地址, 0.0)

    def 奖励摘要(self) -> dict:
        """获取奖励摘要"""
        各类型 = {}
        for r in self._奖励历史:
            各类型[r.类型.value] = 各类型.get(r.类型.value, 0) + r.金额HKAIC

        return {
            "总奖励金额": f"{sum(r.金额HKAIC for r in self._奖励历史):.2f} HKAIC",
            "奖励记录数": len(self._奖励历史),
            "保险池余额": f"{self._保险池:.2f} HKAIC",
            "已铸币": f"{self._已铸币:.2f} HKAIC",
            "年铸币上限": f"{self.年铸币上限:.2f} HKAIC",
            "参与者数": len(self._参与者累计),
            "类型分布": 各类型,
        }
