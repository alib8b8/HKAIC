"""
HKC 变异沙盒 (mutation_sandbox.py)
====================================
变异先在沙盒中测试，不直接影响线上合约。
沙盒模拟链上状态，注入真实历史数据，进行压力测试。
通过标准的变异合约才允许上线。

核心功能：
  - 沙盒环境：模拟链上状态，注入真实历史数据
  - 压力测试：极端场景下变异合约的表现
  - 安全检查：变异后的合约参数是否在安全边界内
  - 通过标准：沙盒评分>当前合约评分的80%才允许上线

纯Python标准库，零外部依赖。
"""

import hashlib
import math
import os
import time
import copy
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple
from enum import Enum

from .contract_genome import 合约基因组
from .fitness_evaluator import 适应性评估器, 评估结果


class 沙盒状态(Enum):
    """沙盒测试状态"""
    初始化 = "initialized"
    运行中 = "running"
    通过 = "passed"
    失败 = "failed"
    超时 = "timeout"


@dataclass
class 压力测试场景:
    """压力测试场景定义"""
    名称: str
    描述: str
    参数覆盖: Dict[str, float] = field(default_factory=dict)  # 覆盖模拟参数
    极端条件: Dict[str, float] = field(default_factory=dict)   # 极端市场条件
    权重: float = 1.0  # 场景权重（影响综合评分）


@dataclass
class 沙盒结果:
    """沙盒测试结果"""
    变异合约ID: str
    原始合约ID: str
    状态: 沙盒状态 = 沙盒状态.初始化
    综合评分: float = 0.0
    原始评分: float = 0.0
    通过阈值: float = 0.0     # 80%原始评分
    是否通过: bool = False
    安全检查通过: bool = True
    压力测试结果: List[dict] = field(default_factory=list)
    评估详情: Optional[评估结果] = None
    测试时间: float = 0.0
    耗时秒: float = 0.0
    失败原因: str = ""

    def __post_init__(self):
        if self.测试时间 == 0:
            self.测试时间 = time.time()


class 变异沙盒:
    """
    变异沙盒——变异合约上线的守门人

    流程：
    1. 变异合约进入沙盒
    2. 安全边界检查
    3. 注入历史数据模拟
    4. 执行压力测试
    5. 评估综合表现
    6. 与原始合约比较，决定是否通过

    通过标准：
      - 安全检查全部通过
      - 沙盒评分 > 原始合约评分 × 80%
      - 压力测试未出现崩溃

    与进化治理联动：
      - 通过沙盒的变异才可进入治理投票
      - 紧急变异可降低通过标准
    """

    # 通过阈值：沙盒评分需达到原始评分的80%
    默认通过阈值比例 = 0.80

    # 预设压力测试场景
    预设压力场景: List[压力测试场景] = [
        压力测试场景(
            名称="高交易量",
            描述="模拟交易量10倍于日常的场景",
            参数覆盖={"交易量": 5000, "用户数": 500},
            极端条件={"市场波动率": 0.5},
            权重=1.0,
        ),
        压力测试场景(
            名称="低流动性",
            描述="模拟流动性骤降的场景",
            参数覆盖={"交易量": 10, "用户数": 2},
            极端条件={"流动性因子": 0.1},
            权重=1.2,
        ),
        压力测试场景(
            名称="价格剧烈波动",
            描述="模拟市场价格剧烈波动的场景",
            参数覆盖={"交易量": 500, "用户数": 100},
            极端条件={"市场波动率": 0.8, "价格变化": -0.3},
            权重=1.5,
        ),
        压力测试场景(
            名称="攻击模拟",
            描述="模拟恶意攻击场景",
            参数覆盖={"交易量": 10000, "异常交易": 500, "用户数": 50},
            极端条件={"攻击强度": 0.7},
            权重=2.0,
        ),
        压力测试场景(
            名称="网络拥堵",
            描述="模拟网络拥堵场景",
            参数覆盖={"交易量": 2000, "用户数": 200},
            极端条件={"Gas倍数": 5.0, "确认延迟": 60},
            权重=1.0,
        ),
    ]

    def __init__(self, 评估器: Optional[适应性评估器] = None,
                 通过阈值比例: float = 0.80):
        """初始化变异沙盒

        参数:
            评估器: 适应性评估器
            通过阈值比例: 通过标准（相对原始评分的比例）
        """
        self._评估器 = 评估器 or 适应性评估器()
        self._通过阈值比例 = 通过阈值比例

        # 历史数据缓存（用于注入模拟）
        self._历史数据: List[dict] = []
        # 沙盒测试结果缓存
        self._测试结果: Dict[str, 沙盒结果] = {}
        # 沙盒模拟链状态
        self._模拟链状态: Dict[str, float] = {
            "当前区块高度": 1000000,
            "全网TPS": 1000,
            "Gas价格": 1.0,
            "HKAIC价格": 1.0,
            "市场波动率": 0.1,
        }

    def 注入历史数据(self, 数据: List[dict]):
        """注入真实历史数据用于沙盒模拟

        参数:
            数据: 历史运行数据列表
        """
        self._历史数据.extend(数据)

    def 设置模拟链状态(self, 状态: Dict[str, float]):
        """设置沙盒模拟的链状态"""
        self._模拟链状态.update(状态)

    def 测试变异(self, 变异基因组: 合约基因组, 原始基因组: 合约基因组,
                  紧急模式: bool = False) -> 沙盒结果:
        """在沙盒中测试变异合约

        参数:
            变异基因组: 变异后的合约基因组
            原始基因组: 原始合约基因组
            紧急模式: 紧急变异模式，降低通过标准
        返回:
            沙盒测试结果
        """
        开始时间 = time.time()
        结果 = 沙盒结果(
            变异合约ID=变异基因组.合约ID,
            原始合约ID=原始基因组.合约ID,
        )

        try:
            结果.状态 = 沙盒状态.运行中

            # 1. 安全边界检查
            不安全参数 = 变异基因组.获取不安全参数()
            if 不安全参数 and not 紧急模式:
                结果.状态 = 沙盒状态.失败
                结果.安全检查通过 = False
                结果.失败原因 = f"安全边界检查未通过: {', '.join(不安全参数)}"
                结果.耗时秒 = time.time() - 开始时间
                self._测试结果[变异基因组.合约ID] = 结果
                return 结果

            # 紧急模式下记录但不阻断
            if 不安全参数 and 紧急模式:
                结果.安全检查通过 = False

            # 2. 注入历史数据模拟
            self._注入模拟数据(变异基因组)

            # 3. 评估变异合约
            变异评估 = self._评估器.评估(变异基因组)
            结果.评估详情 = 变异评估
            结果.综合评分 = 变异评估.综合评分

            # 4. 评估原始合约
            原始评估 = self._评估器.评估(原始基因组)
            结果.原始评分 = 原始评估.综合评分

            # 5. 执行压力测试
            压力结果 = self._执行压力测试(变异基因组)
            结果.压力测试结果 = 压力结果

            # 检查压力测试是否全部通过
            压力失败 = any(not r.get("通过", True) for r in 压力结果)
            if 压力失败 and not 紧急模式:
                结果.状态 = 沙盒状态.失败
                结果.失败原因 = "压力测试未通过"
                结果.耗时秒 = time.time() - 开始时间
                self._测试结果[变异基因组.合约ID] = 结果
                return 结果

            # 6. 计算压力测试调整后的评分
            if 压力结果:
                压力调整 = sum(
                    r.get("评分", 100) * r.get("场景权重", 1.0)
                    for r in 压力结果
                ) / sum(r.get("场景权重", 1.0) for r in 压力结果)
                # 综合评分取模拟评估和压力测试的加权平均
                结果.综合评分 = 变异评估.综合评分 * 0.6 + 压力调整 * 0.4

            # 7. 比较通过标准
            通过阈值 = 结果.原始评分 * self._通过阈值比例
            if 紧急模式:
                通过阈值 = 结果.原始评分 * 0.60  # 紧急模式降低标准

            结果.通过阈值 = 通过阈值
            结果.是否通过 = 结果.综合评分 >= 通过阈值

            if 结果.是否通过:
                结果.状态 = 沙盒状态.通过
            else:
                结果.状态 = 沙盒状态.失败
                结果.失败原因 = (
                    f"沙盒评分{结果.综合评分:.2f}低于阈值{通过阈值:.2f}"
                    f"（原始评分{结果.原始评分:.2f}的{self._通过阈值比例:.0%}）"
                )

        except Exception as e:
            结果.状态 = 沙盒状态.失败
            结果.失败原因 = f"沙盒测试异常: {str(e)}"

        结果.耗时秒 = time.time() - 开始时间
        self._测试结果[变异基因组.合约ID] = 结果
        return 结果

    def _注入模拟数据(self, 基因组: 合约基因组):
        """向评估器注入模拟数据

        使用历史数据和当前基因组参数生成模拟运行数据
        """
        参数 = 基因组.基因表达()

        # 基于基因组参数生成模拟运行数据
        基准利率 = 参数.get("基准利率", 0.05)
        手续费率 = 参数.get("手续费率", 0.003)
        涌现权重 = 参数.get("涌现分数权重", 1.0)

        # 模拟收益：利率越高收益越高，但过高风险也高
        模拟收益 = 100 * 基准利率 / 0.05
        if 基准利率 > 0.2:
            模拟收益 *= (1 - (基准利率 - 0.2) * 2)  # 过高利率扣减

        # 模拟交易量：低手续费吸引更多交易
        模拟交易量 = int(200 * (0.005 / max(手续费率, 0.0001)))
        模拟交易量 = min(模拟交易量, 10000)

        # 模拟用户数
        模拟用户数 = int(模拟交易量 * 0.1)

        # 模拟异常交易
        模拟异常 = max(0, int(模拟交易量 * 0.01 * (1 if 基准利率 > 0.15 else 0)))

        # 收益分布
        基础收益 = 模拟收益 / max(模拟用户数, 1)
        模拟分布 = [基础收益 * (0.5 + (i % 5) * 0.3) for i in range(min(模拟用户数, 20))]

        # 注入到评估器
        self._评估器.记录运行数据(基因组.合约ID, {
            "收益": 模拟收益,
            "交易量": 模拟交易量,
            "用户数": 模拟用户数,
            "异常交易": 模拟异常,
            "最大单笔": 模拟收益 * 0.3,
            "最小单笔": 模拟收益 * 0.01,
            "收益分布": 模拟分布,
        })

        # 也注入历史数据
        for 历史记录 in self._历史数据[-5:]:
            self._评估器.记录运行数据(基因组.合约ID, 历史记录)

    def _执行压力测试(self, 基因组: 合约基因组) -> List[dict]:
        """执行压力测试场景

        参数:
            基因组: 变异后的合约基因组
        返回:
            压力测试结果列表
        """
        结果列表 = []
        参数 = 基因组.基因表达()

        for 场景 in self.预设压力场景:
            场景评分 = self._评估压力场景(基因组, 场景)
            通过 = 场景评分 >= 40.0  # 压力测试最低40分通过

            结果列表.append({
                "场景名称": 场景.名称,
                "评分": 场景评分,
                "通过": 通过,
                "场景权重": 场景.权重,
            })

        return 结果列表

    def _评估压力场景(self, 基因组: 合约基因组, 场景: 压力测试场景) -> float:
        """评估变异合约在特定压力场景下的表现

        使用合约参数和历史数据模拟极端条件下的表现
        """
        参数 = 基因组.基因表达()
        基准利率 = 参数.get("基准利率", 0.05)
        清算阈值 = 参数.get("清算阈值", 0.8)
        手续费率 = 参数.get("手续费率", 0.003)
        保险池抽成 = 参数.get("保险池抽成", 0.01)

        评分 = 70.0  # 基础分

        if 场景.名称 == "高交易量":
            # 高交易量时手续费率和利率是否合理
            if 手续费率 < 0.005:
                评分 += 10
            else:
                评分 -= 15
            if 基准利率 < 0.15:
                评分 += 5

        elif 场景.名称 == "低流动性":
            # 低流动性时利率不应过高
            if 基准利率 > 0.2:
                评分 -= 20
            if 清算阈值 > 0.85:
                评分 -= 15  # 清算阈值过高在低流动性时危险
            if 保险池抽成 > 0.02:
                评分 += 5  # 高保险池抽成在低流动性时更有保障

        elif 场景.名称 == "价格剧烈波动":
            # 价格波动时清算阈值很关键
            if 清算阈值 < 0.75:
                评分 += 15  # 较低清算阈值更安全
            elif 清算阈值 > 0.85:
                评分 -= 25  # 过高清算阈值容易连环清算
            if 基准利率 > 0.15:
                评分 -= 10

        elif 场景.名称 == "攻击模拟":
            # 攻击时保险池和异常检测很关键
            if 保险池抽成 >= 0.01:
                评分 += 10
            if 清算阈值 < 0.8:
                评分 += 5
            if 手续费率 < 0.001:
                评分 -= 10  # 手续费太低容易被DDoS

        elif 场景.名称 == "网络拥堵":
            # 拥堵时手续费调节很重要
            if 0.001 <= 手续费率 <= 0.005:
                评分 += 10
            if 基准利率 < 0.1:
                评分 += 5

        return max(0.0, min(100.0, 评分))

    def 获取测试结果(self, 变异合约ID: str) -> Optional[沙盒结果]:
        """获取沙盒测试结果"""
        return self._测试结果.get(变异合约ID)

    def 沙盒摘要(self) -> dict:
        """获取沙盒摘要"""
        总测试 = len(self._测试结果)
        通过数 = sum(1 for r in self._测试结果.values() if r.是否通过)
        return {
            "总测试数": 总测试,
            "通过数": 通过数,
            "通过率": f"{通过数 / max(总测试, 1):.1%}",
            "历史数据量": len(self._历史数据),
        }
