"""
HKC 涌现聚合引擎 (aggregation_engine.py)
========================================
AI原生的智能数据聚合，不只是中位数，而是动态可信度加权的涌现聚合。
纯Python标准库，零外部依赖。
中文代码风格。
"""

import time
import math
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple
from collections import defaultdict

from oracle.oracle_core import 数据点, 聚合结果, 数据类型


@dataclass
class 数据源可信度:
    """数据源可信度记录"""
    数据源: str = ""
    总报价次数: int = 0
    正常报价次数: int = 0
    偏离中位数次数: int = 0
    累计偏离幅度: float = 0.0
    历史权重: float = 1.0          # 历史可信度权重
    实时权重: float = 1.0          # 实时偏离权重
    综合权重: float = 1.0         # 综合权重
    首次报价时间: float = 0.0
    最后报价时间: float = 0.0
    是否在黑名单: bool = False
    黑名单原因: str = ""

    def __post_init__(self):
        if self.首次报价时间 == 0:
            self.首次报价时间 = time.time()
        if self.最后报价时间 == 0:
            self.最后报价时间 = time.time()


@dataclass
class 涌现聚合配置:
    """涌现聚合配置参数"""
    # 异常检测参数
    偏离阈值: float = 0.05         # 5%偏离阈值
    异常标准差倍数: float = 2.0    # 2σ检测
    最小数据源数: int = 2          # 最少数据源数
    
    # 权重参数
    冷启动初始权重: float = 0.3    # 新数据源初始权重
    正常报价奖励: float = 0.05     # 正常报价权重奖励
    偏离惩罚: float = 0.2          # 偏离惩罚力度
    最大权重上限: float = 3.0       # 最大权重倍数
    最小权重下限: float = 0.1      # 最小权重倍数
    
    # 涌现信号参数
    涌现信号阈值: float = 0.03     # 3%同步偏离视为涌现信号
    涌现信号确认数: int = 2        # 至少2个源同步
    
    # 置信区间参数
    置信区间宽度: float = 0.02     # 2%默认置信区间
    
    # TWAP参数
    TWAP窗口: int = 5              # TWAP窗口大小


class 涌现聚合引擎:
    """
    HKC 涌现聚合引擎
    
    AI原生创新点：
    1. 动态可信度加权：不是简单平均，是AI驱动的智能聚合
    2. 历史行为学习：每个数据源有可信赖度记录
    3. 异常检测：偏离聚合值>2σ的数据源自动降权
    4. 冷启动机制：新数据源权重从低开始
    5. 涌现信号识别：多个独立源同步偏离→可能是真实市场变化
    
    聚合算法：
    a. 收集所有数据源的报价
    b. 计算每个数据源的历史可信度
    c. 动态权重：可信度高的源权重高
    d. 异常检测：偏离大的源权重降低
    e. 涌现信号：如果多个源同步偏离→可能是真实变化
    f. 输出：加权均值 + 置信区间 + 偏离度
    """

    def __init__(self, 配置: Optional[涌现聚合配置] = None):
        # 配置
        self._配置 = 配置 or 涌现聚合配置()
        
        # 数据源可信度: {数据源: 可信度记录}
        self._数据源可信度: Dict[str, 数据源可信度] = {}
        
        # 价格历史用于TWAP: {数据源: [价格]}
        self._价格历史: Dict[str, List[float]] = defaultdict(list)
        
        # 历史中位数: {数据源: [中位数]}
        self._中位数历史: Dict[str, List[float]] = defaultdict(list)
        
        # 聚合结果缓存
        self._最新聚合: Dict[str, 聚合结果] = {}
        
        # 统计数据
        self._聚合次数: int = 0
        self._异常剔除次数: int = 0
        self._涌现信号次数: int = 0

    def 设置配置(self, 配置: 涌现聚合配置) -> None:
        """设置配置"""
        self._配置 = 配置

    def 更新可信度(self, 数据源: str, 是否正常: bool, 偏离幅度: float = 0) -> None:
        """
        更新数据源可信度
        
        参数:
            数据源: 数据源名称
            是否正常: 这次报价是否正常
            偏离幅度: 偏离中位数的幅度
        """
        if 数据源 not in self._数据源可信度:
            self._数据源可信度[数据源] = 数据源可信度(数据源=数据源)

        可信度 = self._数据源可信度[数据源]
        可信度.总报价次数 += 1
        可信度.最后报价时间 = time.time()

        if 是否正常:
            可信度.正常报价次数 += 1
            # 正常报价奖励
            可信度.历史权重 = min(
                self._配置.最大权重上限,
                可信度.历史权重 + self._配置.正常报价奖励
            )
        else:
            可信度.偏离中位数次数 += 1
            可信度.累计偏离幅度 += abs(偏离幅度)
            # 偏离惩罚
            可信度.历史权重 = max(
                self._配置.最小权重下限,
                可信度.历史权重 - self._配置.偏离惩罚
            )
            
            # 检查是否加入黑名单
            偏离率 = 可信度.偏离中位数次数 / max(1, 可信度.总报价次数)
            if 偏离率 > 0.5 and 可信度.总报价次数 >= 10:
                可信度.是否在黑名单 = True
                可信度.黑名单原因 = f"连续偏离率{偏离率:.1%}"

    def 获取可信度(self, 数据源: str) -> Optional[数据源可信度]:
        """获取数据源可信度"""
        return self._数据源可信度.get(数据源)

    def 获取权重(self, 数据源: str) -> float:
        """
        获取数据源的动态权重
        
        综合考虑：
        1. 历史可信度
        2. 实时偏离
        3. 冷启动因子
        """
        可信度 = self._数据源可信度.get(数据源)
        if 可信度 is None:
            # 冷启动：返回初始权重
            return self._配置.冷启动初始权重

        if 可信度.是否在黑名单:
            return self._配置.最小权重下限

        # 基础权重来自历史可信度
        基础权重 = 可信度.历史权重

        # 实时权重考虑报价次数（越多越稳定）
        稳定性因子 = min(1.0, 可信度.总报价次数 / 20)
        实时权重 = 可信度.历史权重 * (0.7 + 0.3 * 稳定性因子)

        # 综合权重
        综合权重 = (基础权重 * 0.4 + 实时权重 * 0.6)

        # 限制在合理范围
        return max(
            self._配置.最小权重下限,
            min(self._配置.最大权重上限, 综合权重)
        )

    def 计算中位数(self, 价格列表: List[float]) -> float:
        """计算中位数"""
        if not 价格列表:
            return 0.0
        排序列表 = sorted(价格列表)
        n = len(排序列表)
        if n % 2 == 1:
            return 排序列表[n // 2]
        else:
            return (排序列表[n // 2 - 1] + 排序列表[n // 2]) / 2

    def 计算标准差(self, 价格列表: List[float]) -> float:
        """计算标准差"""
        if len(价格列表) < 2:
            return 0.0
        平均值 = sum(价格列表) / len(价格列表)
        方差 = sum((x - 平均值) ** 2 for x in 价格列表) / len(价格列表)
        return math.sqrt(方差)

    def 检测涌现信号(
        self,
        数据点列表: List[数据点],
        中位数: float
    ) -> Tuple[bool, float]:
        """
        检测涌现信号
        
        如果多个独立数据源同时出现同方向偏离，
        可能是反映真实市场变化而非异常。
        
        返回: (是否有涌现信号, 信号强度)
        """
        if 中位数 <= 0:
            return False, 0.0

        # 统计同方向偏离的数据源
        正偏离源: List[Tuple[str, float]] = []  # (数据源, 偏离幅度)
        负偏离源: List[Tuple[str, float]] = []

        for 数据点 in 数据点列表:
            偏离幅度 = (数据点.数值 - 中位数) / 中位数
            if 偏离幅度 > self._配置.涌现信号阈值:
                正偏离源.append((数据点.数据源, 偏离幅度))
            elif 偏离幅度 < -self._配置.涌现信号阈值:
                负偏离源.append((数据点.数据源, -偏离幅度))

        # 判断是否有涌现信号
        目标数量 = self._配置.涌现信号确认数
        if len(正偏离源) >= 目标数量 or len(负偏离源) >= 目标数量:
            偏离方向 = "正" if len(正偏离源) >= len(负偏离源) else "负"
            源列表 = 正偏离源 if 偏离方向 == "正" else 负偏离源
            平均强度 = sum(s[1] for s in 源列表) / len(源列表)
            self._涌现信号次数 += 1
            return True, 平均强度

        return False, 0.0

    def 聚合(
        self,
        数据键: str,
        数据点列表: List[数据点],
        数据类型: 数据类型 = 数据类型.价格
    ) -> Optional[聚合结果]:
        """
        核心聚合方法
        
        步骤：
        1. 验证数据点
        2. 计算中位数
        3. 检测异常和涌现信号
        4. 计算动态权重
        5. 计算加权均值
        6. 计算置信区间
        7. 返回聚合结果
        """
        self._聚合次数 += 1

        # 过滤无效数据点
        有效数据点 = [d for d in 数据点列表 if d.是否有效 and d.数值 > 0]

        if len(有效数据点) < self._配置.最小数据源数:
            return None

        # 计算中位数
        价格列表 = [d.数值 for d in 有效数据点]
        中位数 = self.计算中位数(价格列表)

        if 中位数 <= 0:
            return None

        # 检测涌现信号
        有涌现信号, 涌现强度 = self.检测涌现信号(有效数据点, 中位数)

        # 分类数据源：正常 vs 异常
        正常数据点: List[数据点] = []
        异常数据点: List[数据点] = []

        标准差 = self.计算标准差(价格列表)
        异常阈值 = self._配置.异常标准差倍数 * 标准差

        for 数据点 in 有效数据点:
            偏离幅度 = abs(数据点.数值 - 中位数)

            # 异常检测：偏离>2σ或偏离>5%
            是异常 = (
                偏离幅度 > 异常阈值 and 标准差 > 0
            ) or (
                偏离幅度 / 中位数 > self._配置.偏离阈值
            )

            if 是异常 and not 有涌现信号:
                # 有涌现信号时不剔除，保持原样
                异常数据点.append(数据点)
                self._异常剔除次数 += 1
                self.更新可信度(数据点.数据源, False, 偏离幅度 / 中位数)
            else:
                正常数据点.append(数据点)
                self.更新可信度(数据点.数据源, True)

        # 如果剔除后数据源不足，保留部分异常数据
        if len(正常数据点) < self._配置.最小数据源数:
            正常数据点 = 有效数据点[:]

        # 计算动态权重
        权重列表: List[Tuple[数据点, float]] = []
        for 数据点 in 正常数据点:
            权重 = self.获取权重(数据点.数据源)
            # 考虑数据点置信度
            权重 *= 数据点.置信度
            权重列表.append((数据点, 权重))

        # 计算加权均值
        总权重 = sum(w for _, w in 权重列表)
        if 总权重 <= 0:
            return None

        加权均值 = sum(d.数值 * w for d, w in 权重列表) / 总权重

        # 计算置信区间（基于偏离度）
        最大偏离 = max(abs(d.数值 - 加权均值) for d, _ in 权重列表)
        相对偏离 = 最大偏离 / max(加权均值, 1e-10)

        置信区间宽度 = max(
            self._配置.置信区间宽度,
            相对偏离 * 2
        )

        置信区间下限 = 加权均值 * (1 - 置信区间宽度)
        置信区间上限 = 加权均值 * (1 + 置信区间宽度)

        # 计算最大偏离度
        最大偏离度 = max(
            abs(d.数值 - 加权均值) / max(加权均值, 1e-10)
            for d, _ in 权重列表
        ) if 权重列表 else 0.0

        # 计算综合置信度
        置信度 = min(1.0, len(正常数据点) / 5.0)
        if 有涌现信号:
            置信度 *= (0.9 + 0.1 * 涌现强度)

        # TWAP计算（用于平滑）
        TWAP值 = self._计算TWAP(数据键, 加权均值)

        # 构建结果
        结果 = 聚合结果(
            键=数据键,
            数据类型=数据类型,
            聚合值=TWAP值,  # 使用TWAP平滑值
            中位数=中位数,
            置信区间下限=置信区间下限,
            置信区间上限=置信区间上限,
            置信度=置信度,
            数据源数量=len(正常数据点),
            最大偏离度=最大偏离度,
            加权权重=总权重,
            时间戳=time.time(),
            版本号=int(time.time() * 1000),
        )

        self._最新聚合[数据键] = 结果
        return 结果

    def _计算TWAP(self, 数据键: str, 当前值: float) -> float:
        """
        计算时间加权平均价格
        
        使用最近N个价格点的加权平均，近期权重更高
        """
        if 数据键 not in self._价格历史:
            self._价格历史[数据键] = []

        self._价格历史[数据键].append(当前值)

        # 保持窗口大小
        窗口 = self._配置.TWAP窗口
        历史 = self._价格历史[数据键][-窗口:]

        if not 历史:
            return 当前值

        # 线性递减权重：最近的价格权重最高
        总权重 = sum(range(1, len(历史) + 1))
        加权和 = sum(
            v * (i + 1)
            for i, v in enumerate(历史)
        )

        return 加权和 / 总权重

    def 获取TWAP(self, 数据键: str) -> Optional[float]:
        """获取TWAP值"""
        历史 = self._价格历史.get(数据键, [])
        if not 历史:
            return None
        窗口 = self._配置.TWAP窗口
        return self._计算TWAP(数据键, 历史[-1])

    def 获取黑名单数据源(self) -> List[str]:
        """获取黑名单中的数据源"""
        return [
            k for k, v in self._数据源可信度.items()
            if v.是否在黑名单
        ]

    def 移出黑名单(self, 数据源: str) -> bool:
        """移出黑名单"""
        可信度 = self._数据源可信度.get(数据源)
        if 可信度:
            可信度.是否在黑名单 = False
            可信度.黑名单原因 = ""
            # 重置信度
            可信度.历史权重 = self._配置.冷启动初始权重
            可信度.偏离中位数次数 = 0
            return True
        return False

    def 获取聚合统计(self) -> Dict:
        """获取聚合统计"""
        return {
            "聚合次数": self._聚合次数,
            "异常剔除次数": self._异常剔除次数,
            "涌现信号次数": self._涌现信号次数,
            "追踪数据源数": len(self._数据源可信度),
            "黑名单数据源数": len(self.获取黑名单数据源()),
            "最新聚合数": len(self._最新聚合),
        }

    def 重置信度(self, 数据源: str) -> bool:
        """
        重置信度：清除数据源的历史记录，重新开始
        
        用于数据源恢复正常后重新建立信任
        """
        if 数据源 in self._数据源可信度:
            可信度 = self._数据源可信度[数据源]
            可信度.总报价次数 = 0
            可信度.正常报价次数 = 0
            可信度.偏离中位数次数 = 0
            可信度.累计偏离幅度 = 0.0
            可信度.历史权重 = self._配置.冷启动初始权重
            可信度.实时权重 = self._配置.冷启动初始权重
            可信度.是否在黑名单 = False
            可信度.黑名单原因 = ""
            return True
        return False
