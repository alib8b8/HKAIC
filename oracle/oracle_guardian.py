import time
import math
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Callable, Any, Tuple
from enum import Enum
from collections import deque


class 守护者状态(Enum):
    """守护者状态"""
    正常 = "normal"
    警告 = "warning"
    降级 = "degraded"
    紧急 = "emergency"
    暂停 = "paused"


class 告警级别(Enum):
    """告警级别"""
    信息 = "info"
    注意 = "notice"
    警告 = "warning"
    错误 = "error"
    紧急 = "critical"


@dataclass
class 守护者告警:
    """守护者告警"""
    告警ID: str = ""
    级别: 告警级别 = 告警级别.信息
    类别: str = ""                     # "新鲜度" / "可用性" / "合理性" / "质量"
    描述: str = ""
    数据键: str = ""
    详情: Dict[str, Any] = field(default_factory=dict)
    时间戳: float = 0.0
    已确认: bool = False
    已处理: bool = False

    def __post_init__(self):
        if self.时间戳 == 0:
            self.时间戳 = time.time()
        if not self.告警ID:
            import hashlib
            内容 = f"{self.类别}:{self.描述}:{self.时间戳}"
            self.告警ID = hashlib.sha256(内容.encode()).hexdigest()[:16]


@dataclass
class 健康评分详情:
    """健康评分详情"""
    总分: float = 100.0
    新鲜度得分: float = 25.0          # 数据新鲜度
    可用性得分: float = 25.0          # 数据源可用性
    合理性得分: float = 25.0          # 数据合理性
    质量得分: float = 25.0            # 聚合质量
    异常标记: List[str] = field(default_factory=list)


@dataclass
class 守护者配置:
    """守护者配置"""
    # 新鲜度监控
    新鲜度警告阈值: float = 30.0      # 30秒未更新警告
    新鲜度错误阈值: float = 60.0      # 60秒未更新错误
    新鲜度紧急阈值: float = 120.0     # 120秒未更新紧急
    
    # 可用性监控
    最小数据源数: int = 3             # 最少数据源数
    最低可用率: float = 0.8           # 最低可用率
    
    # 合理性检查
    允许价格为零: bool = False        # 是否允许价格为0
    允许价格为负: bool = False        # 是否允许价格为负
    最大单块变化率: float = 0.50      # 最大单块价格变化50%
    
    # 质量监控
    最小聚合质量: float = 0.5         # 最小聚合置信度
    
    # 健康评分阈值
    健康评分正常: float = 80.0        # >=80 正常
    健康评分警告: float = 60.0        # >=60 警告
    健康评分降级: float = 40.0        # >=40 降级
    健康评分紧急: float = 20.0        # <40 紧急
    
    # 紧急暂停
    紧急暂停阈值: float = 10.0        # 健康评分低于此值暂停
    自动恢复检查间隔: float = 60.0    # 自动恢复检查间隔


class 预言机守护者:
    """
    HKC 预言机守护者
    
    职责：
    1. 数据新鲜度监控：价格超过N秒未更新预警
    2. 数据源可用性监控：数据源下线自动切换
    3. 价格合理性检查：不能为0/负数/单块变化过大
    4. 聚合质量监控：数据源数量<3降级
    5. 健康评分：综合评估预言机可靠性
    6. 紧急暂停：异常时通知所有模块暂停
    
    预警级别：
    - 信息(Info): 正常操作信息
    - 注意(Notice): 需要关注但无紧急问题
    - 警告(Warning): 需要处理的问题
    - 错误(Error): 功能受损
    - 紧急(Critical): 系统故障
    """

    def __init__(self, 配置: Optional[守护者配置] = None):
        self._配置 = 配置 or 守护者配置()
        
        # 状态
        self._状态: 守护者状态 = 守护者状态.正常
        
        # 健康评分详情
        self._健康评分详情: Dict[str, 健康评分详情] = {}
        
        # 告警记录
        self._告警: deque = deque(maxlen=1000)
        
        # 紧急暂停回调
        self._暂停回调: List[Callable] = []
        self._恢复回调: List[Callable] = []
        
        # 状态变化回调
        self._状态变化回调: List[Callable[[守护者状态, 守护者状态], None]] = []
        
        # 数据追踪: {数据键: 最后更新时间}
        self._数据更新时间: Dict[str, float] = {}
        
        # 数据源健康: {数据源: 可用率}
        self._数据源健康: Dict[str, float] = {}
        
        # 统计数据
        self._检查次数: int = 0
        self._告警次数: int = 0
        self._暂停次数: int = 0
        self._恢复次数: int = 0
        
        # 最后状态
        self._上次状态: 守护者状态 = 守护者状态.正常
        self._暂停开始时间: float = 0
        self._暂停持续时间: float = 0

    # ==================== 状态管理 ====================

    @property
    def 状态(self) -> 守护者状态:
        """获取当前状态"""
        return self._状态

    @property
    def 是否暂停(self) -> bool:
        """是否处于暂停状态"""
        return self._状态 == 守护者状态.暂停

    def 设置状态(self, 新状态: 守护者状态) -> bool:
        """
        设置状态
        
        如果状态变化，触发回调
        """
        if 新状态 == self._状态:
            return False
        
        旧状态 = self._状态
        self._状态 = 新状态
        
        # 记录状态变化
        if 新状态 == 守护者状态.暂停:
            self._暂停开始时间 = time.time()
            self._暂停次数 += 1
            self._触发暂停回调()
        elif 旧状态 == 守护者状态.暂停:
            self._暂停持续时间 += time.time() - self._暂停开始时间
            self._触发恢复回调()
        
        # 触发状态变化回调
        for 回调 in self._状态变化回调:
            try:
                回调(旧状态, 新状态)
            except Exception:
                pass
        
        # 生成告警
        if 新状态 != 旧状态:
            self._生成告警(
                级别=告警级别.警告 if 新状态 in [
                    守护者状态.警告, 守护者状态.降级
                ] else 告警级别.紧急,
                类别="状态",
                描述=f"预言机状态变化: {旧状态.value} → {新状态.value}"
            )
        
        return True

    # ==================== 新鲜度监控 ====================

    def 更新数据时间(self, 数据键: str, 时间戳: float = 0) -> None:
        """
        更新数据时间
        
        参数:
            数据键: 数据键
            时间戳: 时间戳
        """
        if 时间戳 == 0:
            时间戳 = time.time()
        self._数据更新时间[数据键] = 时间戳

    def 检查新鲜度(self, 数据键: str) -> Tuple[bool, str, 告警级别]:
        """
        检查数据新鲜度
        
        返回: (是否正常, 描述, 告警级别)
        """
        最后时间 = self._数据更新时间.get(数据键)
        
        if 最后时间 is None:
            return True, "无历史数据", 告警级别.信息
        
        当前时间 = time.time()
        年龄 = 当前时间 - 最后时间
        
        if 年龄 <= self._配置.新鲜度警告阈值:
            return True, f"数据新鲜 (年龄{年龄:.1f}秒)", 告警级别.信息
        elif 年龄 <= self._配置.新鲜度错误阈值:
            self._生成告警(
                级别=告警级别.警告,
                类别="新鲜度",
                描述=f"数据过期警告: {数据键} 已{年龄:.1f}秒未更新",
                数据键=数据键,
                详情={"年龄": 年龄}
            )
            return False, f"数据过期警告 (年龄{年龄:.1f}秒)", 告警级别.警告
        elif 年龄 <= self._配置.新鲜度紧急阈值:
            self._生成告警(
                级别=告警级别.错误,
                类别="新鲜度",
                描述=f"数据过期错误: {数据键} 已{年龄:.1f}秒未更新",
                数据键=数据键,
                详情={"年龄": 年龄}
            )
            return False, f"数据过期错误 (年龄{年龄:.1f}秒)", 告警级别.错误
        else:
            self._生成告警(
                级别=告警级别.紧急,
                类别="新鲜度",
                描述=f"数据过期紧急: {数据键} 已{年龄:.1f}秒未更新",
                数据键=数据键,
                详情={"年龄": 年龄}
            )
            return False, f"数据过期紧急 (年龄{年龄:.1f}秒)", 告警级别.紧急

    def 获取所有新鲜度状态(self) -> Dict[str, Tuple[float, 告警级别]]:
        """
        获取所有数据的新鲜度状态
        
        返回: {数据键: (年龄秒数, 告警级别)}
        """
        当前时间 = time.time()
        结果: Dict[str, Tuple[float, 告警级别]] = {}
        
        for 数据键, 最后时间 in self._数据更新时间.items():
            年龄 = 当前时间 - 最后时间
            
            if 年龄 <= self._配置.新鲜度警告阈值:
                级别 = 告警级别.信息
            elif 年龄 <= self._配置.新鲜度错误阈值:
                级别 = 告警级别.警告
            elif 年龄 <= self._配置.新鲜度紧急阈值:
                级别 = 告警级别.错误
            else:
                级别 = 告警级别.紧急
            
            结果[数据键] = (年龄, 级别)
        
        return 结果

    # ==================== 可用性监控 ====================

    def 更新数据源健康(self, 数据源: str, 可用率: float) -> None:
        """
        更新数据源健康状态
        
        参数:
            数据源: 数据源名称
            可用率: 可用率 0-1
        """
        self._数据源健康[数据源] = 可用率
        
        if 可用率 < self._配置.最低可用率:
            self._生成告警(
                级别=告警级别.警告,
                类别="可用性",
                描述=f"数据源可用率低: {数据源} 可用率{可用率:.1%}",
                详情={"数据源": 数据源, "可用率": 可用率}
            )

    def 检查可用性(self) -> Tuple[bool, Dict]:
        """
        检查数据源可用性
        
        返回: (是否正常, {数据源: 可用率})
        """
        总数 = len(self._数据源健康)
        正常数 = sum(
            1 for v in self._数据源健康.values()
            if v >= self._配置.最低可用率
        )
        
        是否正常 = 正常数 >= self._配置.最小数据源数
        
        if not 是否正常:
            self._生成告警(
                级别=告警级别.警告,
                类别="可用性",
                描述=f"数据源不足: 只有{正常数}个可用，需求{self._配置.最小数据源数}个",
                详情={"可用数": 正常数, "需求数": self._配置.最小数据源数}
            )
        
        return 是否正常, dict(self._数据源健康)

    # ==================== 合理性检查 ====================

    def 检查价格合理性(
        self,
        数据键: str,
        价格: float,
        上一价格: Optional[float] = None
    ) -> Tuple[bool, str]:
        """
        检查价格合理性
        
        参数:
            数据键: 数据键
            价格: 当前价格
            上一价格: 上一价格
            
        返回: (是否合理, 原因)
        """
        # 检查是否为0
        if 价格 == 0 and not self._配置.允许价格为零:
            self._生成告警(
                级别=告警级别.紧急,
                类别="合理性",
                描述=f"价格为零: {数据键} 价格={价格}",
                数据键=数据键,
                详情={"价格": 价格}
            )
            return False, "价格为零"
        
        # 检查是否为负
        if 价格 < 0 and not self._配置.允许价格为负:
            self._生成告警(
                级别=告警级别.紧急,
                类别="合理性",
                描述=f"价格为负: {数据键} 价格={价格}",
                数据键=数据键,
                详情={"价格": 价格}
            )
            return False, "价格为负"
        
        # 检查单块变化
        if 上一价格 is not None and 上一价格 > 0:
            变化率 = abs(价格 - 上一价格) / 上一价格
            if 变化率 > self._配置.最大单块变化率:
                self._生成告警(
                    级别=告警级别.紧急,
                    类别="合理性",
                    描述=f"单块价格变化过大: {数据键} 变化{变化率:.2%}",
                    数据键=数据键,
                    详情={"价格": 价格, "上一价格": 上一价格, "变化率": 变化率}
                )
                return False, f"单块变化{变化率:.2%}超过阈值"
        
        return True, "合理"

    # ==================== 健康评分 ====================

    def 计算健康评分(
        self,
        数据键: Optional[str] = None
    ) -> 健康评分详情:
        """
        计算预言机健康评分
        
        参数:
            数据键: 数据键，None表示整体评分
            
        返回:
            健康评分详情
        """
        详情 = 健康评分详情()
        
        # 1. 新鲜度得分 (25分)
        新鲜度得分 = 25.0
        if 数据键:
            最后时间 = self._数据更新时间.get(数据键)
            if 最后时间:
                年龄 = time.time() - 最后时间
                if 年龄 > self._配置.新鲜度紧急阈值:
                    新鲜度得分 = 0.0
                elif 年龄 > self._配置.新鲜度错误阈值:
                    新鲜度得分 = 5.0
                elif 年龄 > self._配置.新鲜度警告阈值:
                    新鲜度得分 = 15.0
        else:
            # 整体：取平均新鲜度
            新鲜度状态 = self.获取所有新鲜度状态()
            if 新鲜度状态:
                警告数 = sum(1 for _, 级别 in 新鲜度状态.values() if 级别 != 告警级别.信息)
                新鲜度得分 = max(0, 25 - 警告数 * 5)
        
        详情.新鲜度得分 = 新鲜度得分
        
        # 2. 可用性得分 (25分)
        可用性得分 = 25.0
        可用, _ = self.检查可用性()
        if not 可用:
            健康数 = sum(1 for v in self._数据源健康.values() if v >= self._配置.最低可用率)
            可用性得分 = max(0, 25 * 健康数 / max(1, self._配置.最小数据源数))
        
        详情.可用性得分 = 可用性得分
        
        # 3. 合理性得分 (25分)
        合理性得分 = 25.0
        # 如果有紧急告警，降低合理性得分
        紧急告警数 = len([a for a in self._告警 if a.级别 == 告警级别.紧急 and not a.已处理])
        合理性得分 = max(0, 25 - 紧急告警数 * 5)
        
        详情.合理性得分 = 合理性得分
        
        # 4. 质量得分 (25分)
        质量得分 = 25.0
        if self._状态 == 守护者状态.降级:
            质量得分 = 15.0
        elif self._状态 == 守护者状态.紧急:
            质量得分 = 5.0
        elif self._状态 == 守护者状态.暂停:
            质量得分 = 0.0
        
        详情.质量得分 = 质量得分
        
        # 计算总分
        详情.总分 = (
            详情.新鲜度得分 +
            详情.可用性得分 +
            详情.合理性得分 +
            详情.质量得分
        )
        
        # 添加异常标记
        if 详情.新鲜度得分 < 20:
            详情.异常标记.append("新鲜度异常")
        if 详情.可用性得分 < 20:
            详情.异常标记.append("可用性异常")
        if 详情.合理性得分 < 20:
            详情.异常标记.append("合理性异常")
        if 详情.质量得分 < 20:
            详情.异常标记.append("质量异常")
        
        return 详情

    def 获取健康评分(self, 数据键: Optional[str] = None) -> float:
        """获取健康评分（简化版）"""
        return self.计算健康评分(数据键).总分

    # ==================== 综合检查 ====================

    def 综合检查(
        self,
        数据键: str,
        价格: float,
        数据源数: int,
        上一价格: Optional[float] = None
    ) -> Tuple[bool, str, 守护者状态]:
        """
        综合检查
        
        参数:
            数据键: 数据键
            价格: 当前价格
            数据源数: 使用的数据源数
            上一价格: 上一价格
            
        返回: (是否通过, 描述, 建议状态)
        """
        self._检查次数 += 1
        问题列表: List[str] = []
        
        # 1. 新鲜度检查
        新鲜度正常, _, _ = self.检查新鲜度(数据键)
        if not 新鲜度正常:
            问题列表.append("数据过期")
        
        # 2. 可用性检查
        if 数据源数 < self._配置.最小数据源数:
            问题列表.append(f"数据源不足({数据源数}<{self._配置.最小数据源数})")
        
        # 3. 合理性检查
        合理, _ = self.检查价格合理性(数据键, 价格, 上一价格)
        if not 合理:
            问题列表.append("价格不合理")
        
        # 4. 更新数据时间
        self.更新数据时间(数据键)
        
        # 5. 计算健康评分
        评分详情 = self.计算健康评分(数据键)
        
        # 6. 确定状态
        建议状态: 守护者状态
        if not 问题列表:
            建议状态 = 守护者状态.正常
        elif len(问题列表) == 1:
            建议状态 = 守护者状态.警告
        else:
            建议状态 = 守护者状态.降级
        
        if 评分详情.总分 < self._配置.健康评分紧急:
            建议状态 = 守护者状态.紧急
        
        if 评分详情.总分 < self._配置.紧急暂停阈值:
            建议状态 = 守护者状态.暂停
        
        # 更新状态
        self.设置状态(建议状态)
        
        # 保存评分详情
        self._健康评分详情[数据键] = 评分详情
        
        通过 = len(问题列表) == 0
        描述 = "通过" if 通过 else f"问题: {', '.join(问题列表)}"
        
        return 通过, 描述, 建议状态

    # ==================== 告警管理 ====================

    def _生成告警(
        self,
        级别: 告警级别,
        类别: str,
        描述: str,
        数据键: str = "",
        详情: Optional[Dict] = None
    ) -> None:
        """生成告警"""
        告警 = 守护者告警(
            级别=级别,
            类别=类别,
            描述=描述,
            数据键=数据键,
            详情=详情 or {}
        )
        self._告警.append(告警)
        self._告警次数 += 1

    def 获取告警列表(
        self,
        级别: Optional[告警级别] = None,
        类别: Optional[str] = None,
        未处理: bool = False,
        数量: int = 20
    ) -> List[守护者告警]:
        """获取告警列表"""
        结果 = list(self._告警)
        
        if 级别:
            结果 = [a for a in 结果 if a.级别 == 级别]
        if 类别:
            结果 = [a for a in 结果 if a.类别 == 类别]
        if 未处理:
            结果 = [a for a in 结果 if not a.已处理]
        
        return 结果[-数量:]

    def 确认告警(self, 告警ID: str) -> bool:
        """确认告警"""
        for 告警 in self._告警:
            if 告警.告警ID == 告警ID:
                告警.已确认 = True
                return True
        return False

    def 处理告警(self, 告警ID: str) -> bool:
        """处理告警"""
        for 告警 in self._告警:
            if 告警.告警ID == 告警ID:
                告警.已处理 = True
                return True
        return False

    # ==================== 暂停/恢复 ====================

    def 暂停预言机(self, 原因: str = "") -> None:
        """暂停预言机"""
        self._生成告警(
            级别=告警级别.紧急,
            类别="状态",
            描述=f"预言机暂停: {原因}"
        )
        self.设置状态(守护者状态.暂停)

    def 恢复预言机(self) -> bool:
        """恢复预言机（需手动调用）"""
        if self._状态 == 守护者状态.暂停:
            # 检查是否满足恢复条件
            评分 = self.计算健康评分()
            if 评分.总分 >= self._配置.健康评分警告:
                self.设置状态(守护者状态.正常)
                self._恢复次数 += 1
                return True
        return False

    def 注册暂停回调(self, 回调: Callable[[str], None]) -> None:
        """注册暂停回调"""
        self._暂停回调.append(回调)

    def 注册恢复回调(self, 回调: Callable) -> None:
        """注册恢复回调"""
        self._恢复回调.append(回调)

    def _触发暂停回调(self) -> None:
        """触发暂停回调"""
        原因 = f"预言机暂停，健康评分: {self.计算健康评分().总分:.1f}"
        for 回调 in self._暂停回调:
            try:
                回调(原因)
            except Exception:
                pass

    def _触发恢复回调(self) -> None:
        """触发恢复回调"""
        for 回调 in self._恢复回调:
            try:
                回调()
            except Exception:
                pass

    # ==================== 统计 ====================

    def 获取统计信息(self) -> Dict:
        """获取统计信息"""
        return {
            "当前状态": self._状态.value,
            "检查次数": self._检查次数,
            "告警次数": self._告警次数,
            "暂停次数": self._暂停次数,
            "恢复次数": self._恢复次数,
            "追踪数据键数": len(self._数据更新时间),
            "追踪数据源数": len(self._数据源健康),
            "未处理告警数": len([a for a in self._告警 if not a.已处理]),
            "紧急告警数": len([a for a in self._告警 if a.级别 == 告警级别.紧急 and not a.已处理]),
            "暂停持续时间": self._暂停持续时间,
        }
