import time
import math
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple, Any, Callable
from collections import defaultdict, deque

from oracle.oracle_core import 聚合结果, 数据类型


@dataclass
class 波动率数据:
    """波动率数据"""
    代币对: str = ""
    历史波动率: float = 0.0            # 基于历史的波动率
    涌现波动率: float = 0.0            # AI预测的波动率
    隐含波动率: float = 0.0            # 隐含波动率（期权定价用）
    波动率指数: float = 0.0            # 类似VIX的波动率指数
    时间戳: float = 0.0
    区块高度: int = 0
    置信度: float = 1.0
    元数据: Dict[str, Any] = field(default_factory=dict)

    def __post_init__(self):
        if self.时间戳 == 0:
            self.时间戳 = time.time()

    @property
    def 综合波动率(self) -> float:
        """综合波动率（历史 + 涌现的加权平均）"""
        return (self.历史波动率 * 0.6 + self.涌现波动率 * 0.4)


@dataclass
class 波动率曲面点:
    """波动率曲面点（不同到期时间的隐含波动率）"""
    到期时间: float = 0.0              # 到期时间（年）
    隐含波动率: float = 0.0            # 隐含波动率
    行权价偏离: float = 0.0            # 行权价偏离（OTM/ITM程度）


@dataclass
class 波动率预言机配置:
    """波动率预言机配置"""
    # 历史波动率参数
    历史波动率窗口: int = 20            # 计算窗口
    年化天数: float = 365.25           # 年化天数
    
    # 涌现波动率参数
    涌现预测窗口: int = 10             # 预测窗口
    涌现学习率: float = 0.1             # 学习率
    涌现动量: float = 0.3              # 动量因子
    
    # 极端波动预警
    极端波动阈值: float = 0.5          # 50%年化波动率视为极端
    预警冷却秒: float = 300            # 预警冷却时间
    
    # 波动率指数
    指数计算窗口: int = 20             # 波动率指数窗口


class 波动率预言机:
    """
    HKC 波动率预言机
    
    AI原生创新点：
    1. 历史波动率：基于过去N个周期价格变化的标准差
    2. 涌现波动率：AI预测未来波动率（根据链上涌现信号+历史模式）
    3. 波动率曲面：不同到期时间的隐含波动率（供期权定价）
    4. 极端波动预警：波动率突破阈值自动通知
    5. 波动率指数：类似VIX的链上波动率指标
    
    与传统预言机的区别：
    - 传统预言机只有价格
    - HKC预言机提供完整的波动率维度
    """

    def __init__(self, 配置: Optional[波动率预言机配置] = None):
        self._配置 = 配置 or 波动率预言机配置()
        
        # 价格历史: {代币对: [(时间戳, 价格, 区块高度)]}
        self._价格历史: Dict[str, deque] = defaultdict(
            lambda: deque(maxlen=500)
        )
        
        # 波动率历史: {代币对: [波动率数据]}
        self._波动率历史: Dict[str, List[波动率数据]] = defaultdict(list)
        
        # 波动率曲面: {代币对: [波动率曲面点]}
        self._波动率曲面: Dict[str, List[波动率曲面点]] = defaultdict(list)
        
        # 极端波动预警订阅: {代币对: [回调]}
        self._预警订阅: Dict[str, List[callable]] = defaultdict(list)
        
        # 预警冷却: {代币对: 下次可预警时间}
        self._预警冷却: Dict[str, float] = {}
        
        # 涌现模型状态: {代币对: 状态}
        self._涌现状态: Dict[str, Dict] = defaultdict(dict)
        
        # 统计数据
        self._计算次数: int = 0
        self._预警次数: int = 0

    # ==================== 价格记录 ====================

    def 记录价格(
        self,
        代币对: str,
        价格: float,
        区块高度: int = 0,
        时间戳: float = 0
    ) -> None:
        """
        记录价格用于波动率计算
        
        参数:
            代币对: e.g., "HKAIC/USDT"
            价格: 价格
            区块高度: 区块高度
            时间戳: 时间戳
        """
        if 时间戳 == 0:
            时间戳 = time.time()
        
        self._价格历史[代币对].append((时间戳, 价格, 区块高度))
        
        # 限制历史大小
        if len(self._价格历史[代币对]) > 500:
            self._价格历史[代币对] = deque(
                list(self._价格历史[代币对])[-500:],
                maxlen=500
            )

    # ==================== 历史波动率 ====================

    def 计算历史波动率(
        self,
        代币对: str,
        窗口: Optional[int] = None
    ) -> float:
        """
        计算历史波动率
        
        基于对数收益率的标准差，年化处理
        
        参数:
            代币对: 代币对
            窗口: 计算窗口，默认使用配置
            
        返回:
            年化波动率（0-1表示百分比）
        """
        窗口 = 窗口 or self._配置.历史波动率窗口
        历史 = list(self._价格历史.get(代币对, []))
        
        if len(历史) < 窗口 + 1:
            return 0.0
        
        # 取最近窗口的价格
        价格列表 = [p for _, p, _ in 历史[-窗口-1:]]
        
        # 计算对数收益率
        收益率列表: List[float] = []
        for i in range(1, len(价格列表)):
            if 价格列表[i-1] > 0 and 价格列表[i] > 0:
                收益率 = math.log(价格列表[i] / 价格列表[i-1])
                收益率列表.append(收益率)
        
        if len(收益率列表) < 2:
            return 0.0
        
        # 计算标准差
        平均收益 = sum(收益率列表) / len(收益率列表)
        方差 = sum((r - 平均收益) ** 2 for r in 收益率列表) / len(收益率列表)
        标准差 = math.sqrt(方差)
        
        # 年化
        年化波动率 = 标准差 * math.sqrt(self._配置.年化天数)
        
        return 年化波动率

    # ==================== 涌现波动率（AI预测）====================

    def 计算涌现波动率(
        self,
        代币对: str,
        预测步数: int = 1
    ) -> float:
        """
        计算涌现波动率（AI预测）
        
        基于历史模式和链上涌现信号预测未来波动率
        
        原理：
        1. 分析历史波动率的趋势和动量
        2. 检测链上活动模式（如交易量变化）
        3. 使用指数平滑预测
        
        参数:
            代币对: 代币对
            预测步数: 预测步数
            
        返回:
            预测的年化波动率
        """
        历史 = list(self._价格历史.get(代币对, []))
        
        if len(历史) < 5:
            return self.计算历史波动率(代币对)
        
        # 获取历史波动率序列
        波动率序列: List[float] = []
        for i in range(2, len(历史)):
            窗口 = min(10, i)
            价格窗口 = [p for _, p, _ in 历史[i-窗口:i]]
            if len(价格窗口) >= 2:
                收益率 = [
                    math.log(价格窗口[j] / max(价格窗口[j-1], 1e-10))
                    for j in range(1, len(价格窗口))
                ]
                if 收益率:
                    标准差 = math.sqrt(
                        sum((r - sum(收益率)/len(收益率))**2 for r in 收益率)
                        / len(收益率)
                    )
                    波动率序列.append(标准差 * math.sqrt(self._配置.年化天数))
        
        if len(波动率序列) < 3:
            return self.计算历史波动率(代币对)
        
        # 获取或初始化涌现状态
        if 代币对 not in self._涌现状态:
            self._涌现状态[代币对] = {
                "预测": 波动率序列[-1] if 波动率序列 else 0,
                "动量": 0.0,
                "趋势": 0.0
            }
        
        状态 = self._涌现状态[代币对]
        
        # 分析近期趋势
        最近波动率 = 波动率序列[-3:] if len(波动率序列) >= 3 else 波动率序列
        趋势 = 0.0
        if len(最近波动率) >= 2:
            趋势 = (最近波动率[-1] - 最近波动率[0]) / max(最近波动率[0], 0.01)
        
        # 计算动量
        动量 = self._配置.涌现动量 * 趋势 + (1 - self._配置.涌现动量) * 状态.get("动量", 0)
        
        # 预测
        历史均值 = sum(波动率序列) / len(波动率序列)
        最近均值 = sum(最近波动率) / len(最近波动率)
        
        # 加权预测：近期 + 历史均值 + 动量
        预测 = (
            最近均值 * (1 - self._配置.涌现学习率) +
            历史均值 * self._配置.涌现学习率 +
            动量 * 最近均值 * 0.1
        )
        
        # 限制范围
        预测 = max(0.01, min(2.0, 预测))
        
        # 更新状态
        状态["预测"] = 预测
        状态["动量"] = 动量
        状态["趋势"] = 趋势
        
        return 预测

    # ==================== 波动率曲面 ====================

    def 计算波动率曲面(
        self,
        代币对: str,
        到期时间列表: Optional[List[float]] = None
    ) -> List[波动率曲面点]:
        """
        计算波动率曲面
        
        不同到期时间的隐含波动率，用于期权定价
        
        参数:
            代币对: 代币对
            到期时间列表: 到期时间列表（年），默认[0.25, 0.5, 1.0]
            
        返回:
            波动率曲面点列表
        """
        if 到期时间列表 is None:
            到期时间列表 = [0.25, 0.5, 1.0]  # 3个月、6个月、1年
        
        历史波动率 = self.计算历史波动率(代币对)
        涌现波动率 = self.计算涌现波动率(代币对)
        
        # 基础隐含波动率
        基础IV = (历史波动率 + 涌现波动率) / 2
        
        曲面点列表: List[波动率曲面点] = []
        
        for 到期时间 in 到期时间列表:
            # 简单模型：长期波动率趋向均值
            # 短期波动率波动更大
            短期因子 = 1.0 + 0.2 / max(到期时间, 0.1)
            隐含波动率 = 基础IV * 短期因子
            
            曲面点 = 波动率曲面点(
                到期时间=到期时间,
                隐含波动率=隐含波动率,
                行权价偏离=0.0  # ATM期权
            )
            曲面点列表.append(曲面点)
        
        # 更新缓存
        self._波动率曲面[代币对] = 曲面点列表
        
        return 曲面点列表

    def 获取波动率曲面(
        self,
        代币对: str
    ) -> List[波动率曲面点]:
        """获取波动率曲面"""
        if 代币对 not in self._波动率曲面:
            return self.计算波动率曲面(代币对)
        return self._波动率曲面[代币对]

    # ==================== 波动率指数 ====================

    def 计算波动率指数(
        self,
        代币对列表: Optional[List[str]] = None
    ) -> float:
        """
        计算波动率指数（类似VIX）
        
        综合多个代币对的波动率
        
        参数:
            代币对列表: 代币对列表，默认使用所有追踪的
            
        返回:
            波动率指数
        """
        if 代币对列表 is None:
            代币对列表 = list(self._价格历史.keys())
        
        if not 代币对列表:
            return 0.0
        
        波动率列表: List[float] = []
        
        for 代币对 in 代币对列表:
            历史波动率 = self.计算历史波动率(代币对)
            if 历史波动率 > 0:
                波动率列表.append(历史波动率)
        
        if not 波动率列表:
            return 0.0
        
        # 使用中位数（更稳健）
        波动率列表.sort()
        n = len(波动率列表)
        if n % 2 == 1:
            中位数 = 波动率列表[n // 2]
        else:
            中位数 = (波动率列表[n // 2 - 1] + 波动率列表[n // 2]) / 2
        
        return 中位数

    # ==================== 极端波动预警 ====================

    def 订阅极端波动(
        self,
        代币对: str,
        回调: callable
    ) -> None:
        """
        订阅极端波动预警
        
        参数:
            代币对: 代币对
            回调: 回调函数(代币对, 波动率, 阈值)
        """
        self._预警订阅[代币对].append(回调)

    def 取消订阅(
        self,
        代币对: str,
        回调: callable
    ) -> bool:
        """取消订阅"""
        if 代币对 in self._预警订阅:
            try:
                self._预警订阅[代币对].remove(回调)
                return True
            except ValueError:
                pass
        return False

    def 检查极端波动(
        self,
        代币对: str,
        当前波动率: float
    ) -> bool:
        """
        检查极端波动并触发预警
        
        参数:
            代币对: 代币对
            当前波动率: 当前年化波动率
            
        返回:
            是否触发了预警
        """
        if 当前波动率 < self._配置.极端波动阈值:
            return False
        
        # 检查冷却
        冷却结束 = self._预警冷却.get(代币对, 0)
        if time.time() < 冷却结束:
            return False
        
        # 触发预警
        self._预警冷却[代币对] = time.time() + self._配置.预警冷却秒
        self._预警次数 += 1
        
        # 调用所有订阅的回调
        for 回调 in self._预警订阅.get(代币对, []):
            try:
                回调(代币对, 当前波动率, self._配置.极端波动阈值)
            except Exception:
                pass
        
        return True

    # ==================== 综合计算 ====================

    def 计算波动率(
        self,
        代币对: str,
        价格: float,
        区块高度: int = 0
    ) -> 波动率数据:
        """
        综合计算波动率
        
        参数:
            代币对: 代币对
            价格: 当前价格
            区块高度: 区块高度
            
        返回:
            波动率数据
        """
        self._计算次数 += 1
        
        # 记录价格
        self.记录价格(代币对, 价格, 区块高度)
        
        # 计算历史波动率
        历史波动率 = self.计算历史波动率(代币对)
        
        # 计算涌现波动率
        涌现波动率 = self.计算涌现波动率(代币对)
        
        # 计算隐含波动率（基于波动率曲面）
        隐含波动率 = 0.0
        曲面 = self.计算波动率曲面(代币对)
        if 曲面:
            # 取1年到期的隐含波动率
            for 点 in 曲面:
                if abs(点.到期时间 - 1.0) < 0.1:
                    隐含波动率 = 点.隐含波动率
                    break
            if 隐含波动率 == 0.0:
                隐含波动率 = 曲面[0].隐含波动率
        
        # 计算波动率指数
        波动率指数 = self.计算波动率指数()
        
        # 构建结果
        结果 = 波动率数据(
            代币对=代币对,
            历史波动率=历史波动率,
            涌现波动率=涌现波动率,
            隐含波动率=隐含波动率,
            波动率指数=波动率指数,
            时间戳=time.time(),
            区块高度=区块高度,
            置信度=min(1.0, 历史波动率 / 0.5) if 历史波动率 > 0 else 0.5
        )
        
        # 保存历史
        self._波动率历史[代币对].append(结果)
        if len(self._波动率历史[代币对]) > 100:
            self._波动率历史[代币对] = self._波动率历史[代币对][-100:]
        
        # 检查极端波动
        self.检查极端波动(代币对, 结果.综合波动率)
        
        return 结果

    def 获取波动率(
        self,
        代币对: str
    ) -> Optional[波动率数据]:
        """获取最新波动率数据"""
        历史 = self._波动率历史.get(代币对, [])
        if 历史:
            return 历史[-1]
        return None

    def 获取波动率历史(
        self,
        代币对: str,
        数量: int = 10
    ) -> List[波动率数据]:
        """获取波动率历史"""
        历史 = self._波动率历史.get(代币对, [])
        return 历史[-数量:]

    # ==================== 期权定价辅助 ====================

    def 获取期权波动率(
        self,
        代币对: str,
        到期时间: float,
        行权价偏离: float = 0.0
    ) -> float:
        """
        获取期权定价用的波动率
        
        参数:
            代币对: 代币对
            到期时间: 到期时间（年）
            行权价偏离: 行权价偏离（0=ATM，正=OTM call，负=OTM put）
            
        返回:
            隐含波动率
        """
        曲面 = self.获取波动率曲面(代币对)
        
        if not 曲面:
            return self.计算历史波动率(代币对)
        
        # 找最近的到期时间
        最近点: Optional[波动率曲面点] = None
        最小差距 = float('inf')
        
        for 点 in 曲面:
            差距 = abs(点.到期时间 - 到期时间)
            if 差距 < 最小差距:
                最小差距 = 差距
                最近点 = 点
        
        if 最近点:
            # 简单波动率微笑模型
            基础IV = 最近点.隐含波动率
            微笑因子 = 1.0 + 0.1 * (行权价偏离 ** 2)  # ATM最便宜，两端升高
            return 基础IV * 微笑因子
        
        return 曲面[0].隐含波动率

    # ==================== 统计 ====================

    def 获取统计信息(self) -> Dict:
        """获取统计信息"""
        return {
            "追踪代币对数": len(self._价格历史),
            "计算次数": self._计算次数,
            "预警次数": self._预警次数,
            "当前波动率指数": self.计算波动率指数(),
            "波动率曲面数": len(self._波动率曲面),
        }
