    # ==================== 数据源状态 ====================

    def 获取数据源状态(self) -> List[Dict]:
        """
        获取所有数据源状态
        
        返回:
            数据源状态列表
        """
        健康报告列表 = self._数据源管理器.获取所有健康报告()
        结果 = []
        
        for 报告 in 健康报告列表:
            结果.append({
                "数据源": 报告.数据源,
                "状态": 报告.状态.value,
                "响应时间": 报告.响应时间,
                "可用率": 报告.可用率,
                "数据新鲜度": 报告.数据新鲜度,
                "健康评分": 报告.健康评分,
                "最后更新时间": 报告.最后更新时间,
            })
        
        return 结果

