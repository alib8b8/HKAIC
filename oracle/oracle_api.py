    # ==================== 内部接口 ====================

    def 验证价格(
        self,
        代币对: str,
        价格: float
    ) -> Tuple[bool, str]:
        """
        验证价格是否在合理范围
        
        参数:
            代币对: 代币对
            价格: 价格
            
        返回: (是否合理, 原因)
        """
        # 获取当前价格
        结果 = self.获取价格(代币对)
        
        if not 结果.成功:
            return False, 结果.错误信息
        
        # 检查是否在置信区间内
        下限, 上限 = 结果.置信区间
        if 价格 < 下限 or 价格 > 上限:
            偏离 = abs(价格 - 结果.价格) / max(结果.价格, 0.01)
            return False, f"价格偏离{偏离:.2%}，超出置信区间"
        
        return True, "合理"

    def 触发检查(self, 代币对: str) -> bool:
        """
        触发立即检查
        
        参数:
            代币对: 代币对
            
        返回:
            是否成功
        """
        # 从数据源拉取数据
        数据源列表 = self._数据源管理器.获取数据源列表()
        
        for 配置 in 数据源列表:
            数据点 = self._数据源管理器.拉取数据(配置.名称, 代币对)
            if 数据点:
                self._核心.添加数据点(数据点)
        
        # 返回最新结果
        return True

