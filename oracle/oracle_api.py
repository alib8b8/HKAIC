@dataclass
class 预言机查询结果:
    """预言机查询结果"""
    成功: bool = False
    数据键: str = ""
    价格: float = 0.0
    置信区间: Tuple[float, float] = (0.0, 0.0)
    TWAP价格: float = 0.0
    数据源数量: int = 0
    置信度: float = 0.0
    偏离度: float = 0.0
    是否延迟确认: bool = False
    健康评分: float = 100.0
    时间戳: float = 0.0
    版本号: int = 0
    错误信息: str = ""

    def __post_init__(self):
        if self.时间戳 == 0:
            self.时间戳 = time.time()


class 预言机API:
    """
    HKC AI原生预言机 API
    
    整合所有预言机模块，提供统一接口：
    
    查询接口：
    - 获取价格(代币对) → 价格+置信区间+TWAP
    - 获取波动率(代币对, 周期) → 历史波动率+涌现预测
    - 获取事件(类型, 时间范围) → 事件列表
    - 获取数据源状态() → 所有数据源健康度
    - 获取预言机健康() → 预言机整体健康评分
    
    管理接口：
    - 注册数据源(类型, 配置) → 管理员权限
    - 订阅更新(代币对, 回调) → 模块订阅
    - 设置阈值(代币对, 参数) → 管理员权限
    
    内部接口（供其他模块调用）：
    - 验证价格(代币对, 价格) → 是否在合理范围
    - 触发检查(代币对) → 立即拉取最新数据
    """

    def __init__(self):
        # 核心组件
        self._核心 = 预言机核心()
        self._聚合引擎 = 涌现聚合引擎()
        self._数据源管理器 = 数据源管理器()
        self._操纵防护 = 操纵防护引擎()
        self._波动率预言机 = 波动率预言机()
        self._事件预言机 = 事件预言机()
        self._守护者 = 预言机守护者()
        
        # 配置
        self._聚合配置 = 涌现聚合配置()
        self._操纵配置 = 操纵防护配置()
        self._波动率配置 = 波动率预言机配置()
        
        # 价格验证回调
        self._价格验证回调: Optional[Callable] = None
        
        # 统计数据
        self._总查询次数: int = 0
        self._成功查询次数: int = 0
        self._失败查询次数: int = 0

    # ==================== 初始化 ====================

    def 初始化默认数据源(self) -> None:
        """初始化默认数据源"""
        # 注册模拟数据源
        默认数据源列表 = [
            ("CoinGecko模拟", 数据源类型.外部API, 1.0),
            ("Binance模拟", 数据源类型.外部API, 0.95),
            ("CoinMarketCap模拟", 数据源类型.外部API, 0.9),
            ("HKC_DEX", 数据源类型.DEX链上, 0.85),
            ("跨链桥接", 数据源类型.跨链桥, 0.8),
        ]
        
        for 名称, 类型, 权重 in 默认数据源列表:
            配置 = 数据源配置(
                名称=名称,
                类型=类型,
                权重=权重
            )
            self._数据源管理器.注册数据源(配置)
            self._核心.注册数据源(名称, 类型)
            
            # 注册模拟拉取回调
            模拟源 = 模拟数据源(名称, 基础价格=100.0, 波动率=0.01)
            self._数据源管理器.注册拉取回调(
                名称,
                lambda 键, cfg, 源=模拟源: 源.拉取价格(键)
            )

    # ==================== 价格查询 ====================

    def 获取价格(self, 代币对: str) -> 预言机查询结果:
        """
        获取价格
        
        参数:
            代币对: e.g., "HKAIC/USDT"
            
        返回:
            预言机查询结果
        """
        self._总查询次数 += 1
        
        # 检查守护者状态
        if self._守护者.是否暂停:
            return 预言机查询结果(
                成功=False,
                数据键=代币对,
                错误信息="预言机已暂停"
            )
        
        try:
            # 从核心获取活跃数据
            活跃数据 = self._核心.获取活跃数据(代币对, 最大年龄=60)
            
            # 转换为数据点列表
            数据点列表: List[数据点] = []
            数据源价格列表: List[Tuple[str, float]] = []
            
            for 数据点 in 活跃数据:
                数据点列表.append(数据点)
                数据源价格列表.append((数据点.数据源, 数据点.数值))
            
            # 如果没有活跃数据，返回失败
            if not 数据点列表:
                self._失败查询次数 += 1
                return 预言机查询结果(
                    成功=False,
                    数据键=代币对,
                    错误信息="无活跃数据"
                )
            
            # 聚合
            聚合结果 = self._聚合引擎.聚合(代币对, 数据点列表)
            
            if 聚合结果 is None:
                self._失败查询次数 += 1
                return 预言机查询结果(
                    成功=False,
                    数据键=代币对,
                    错误信息="聚合失败"
                )
            
            # 操纵检测
            安全, 操纵事件, 原因 = self._操纵防护.综合检测(
                代币对, 聚合结果,
                self._核心.当前区块高度,
                数据源价格列表
            )
            
            if not 安全:
                self._失败查询次数 += 1
                return 预言机查询结果(
                    成功=False,
                    数据键=代币对,
                    价格=聚合结果.聚合值,
                    错误信息=原因
                )
            
            # 更新核心的聚合结果
            self._核心.设置聚合结果(聚合结果)
            
            # 更新守护者
            上一聚合 = self._核心.获取历史数据(代币对, 数量=2)
            上一价格 = 上一聚合[-1].聚合值 if len(上一聚合) >= 2 else None
            
            通过, 描述, _ = self._守护者.综合检查(
                代币对,
                聚合结果.聚合值,
                聚合结果.数据源数量,
                上一价格
            )
            
            # 获取TWAP
            TWAP = self._聚合引擎.获取TWAP(代币对) or 聚合结果.聚合值
            
            # 获取健康评分
            健康评分 = self._守护者.获取健康评分(代币对)
            
            # 更新波动率
            self._波动率预言机.计算波动率(
                代币对,
                聚合结果.聚合值,
                self._核心.当前区块高度
            )
            
            self._成功查询次数 += 1
            
            return 预言机查询结果(
                成功=True,
                数据键=代币对,
                价格=聚合结果.聚合值,
                置信区间=(聚合结果.置信区间下限, 聚合结果.置信区间上限),
                TWAP价格=TWAP,
                数据源数量=聚合结果.数据源数量,
                置信度=聚合结果.置信度,
                偏离度=聚合结果.最大偏离度,
                是否延迟确认=聚合结果.是否延迟确认,
                健康评分=健康评分,
                时间戳=聚合结果.时间戳,
                版本号=聚合结果.版本号
            )
            
        except Exception as e:
            self._失败查询次数 += 1
            return 预言机查询结果(
                成功=False,
                数据键=代币对,
                错误信息=str(e)
            )

    def 获取价格从数据源(
        self,
        代币对: str,
        数据源: str
    ) -> Optional[float]:
        """
        从指定数据源获取价格
        
        参数:
            代币对: 代币对
            数据源: 数据源名称
            
        返回:
            价格或None
        """
        活跃数据 = self._核心.获取活跃数据(代币对, 最大年龄=60)
        for 数据点 in 活跃数据:
            if 数据点.数据源 == 数据源:
                return 数据点.数值
        return None

    def 获取TWAP(
        self,
        代币对: str,
        窗口: int = 5
    ) -> Optional[float]:
        """
        获取TWAP
        
        参数:
            代币对: 代币对
            窗口: TWAP窗口
            
        返回:
            TWAP价格
        """
        return self._聚合引擎.获取TWAP(代币对)

